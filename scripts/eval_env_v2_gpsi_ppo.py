def checkpoint_step(path: Path) -> int:
    name = path.name
    patterns = [r"checkpoint_(\d+)k", r"step(\d+)", r"step_(\d+)", r"(\d+)k"]
    for pattern in patterns:
        match = re.search(pattern, name)
        if match:
            value = int(match.group(1))
            return value * 1000 if "k" in pattern or "k" in match.group(0) else value
    return 0


def make_gpsi_env(args: argparse.Namespace, scenario: str) -> GpsiObsWrapper:
    env = DynamicObstacleFlowEnv(scenario=scenario)
    wrapped = GpsiObsWrapper(
        env,
        gpsi_checkpoint=ROOT / args.gpsi_checkpoint,
        device=args.device,
        history_steps=args.history_steps,
        delta_scale=args.delta_scale,
        logvar_clamp=(float(args.logvar_clamp[0]), float(args.logvar_clamp[1])),
    )
    freeze = wrapped.freeze_check
    if freeze.training or freeze.requires_grad_any or freeze.trainable_parameters != 0:
        raise PhaseN3EvalStop("gpsi_not_frozen", f"Gpsi freeze check failed in eval: {freeze}")
    if int(wrapped.observation_space["obs"].shape[-1]) != 94:
        raise PhaseN3EvalStop("schema_mismatch", f"expected aug obs dim 94, got {wrapped.observation_space['obs'].shape[-1]}")
    return wrapped


def attention_snapshot(model: PPO, max_obs: int) -> tuple[np.ndarray, float]:
    extractor = getattr(getattr(model, "policy", None), "features_extractor", None)
    weights = getattr(extractor, "latest_attention_weights", None)
    entropy = getattr(extractor, "latest_attention_entropy", None)
    if weights is None:
        arr = np.full(max_obs, np.nan, dtype=np.float32)
    else:
        arr = weights.detach().cpu().numpy()
        if arr.ndim >= 2:
            arr = arr[0]
        arr = np.asarray(arr, dtype=np.float32)
        if arr.shape[0] < max_obs:
            arr = np.pad(arr, (0, max_obs - arr.shape[0]), constant_values=np.nan)
    if entropy is None:
        entropy_value = float("nan")
    else:
        entropy_arr = entropy.detach().cpu().numpy()
        entropy_value = float(np.ravel(entropy_arr)[0]) if entropy_arr.size else float("nan")
    return arr[:max_obs], entropy_value


def raw_action_diagnostics(action: np.ndarray, info: dict[str, Any], env: DynamicObstacleFlowEnv, args: argparse.Namespace) -> dict[str, Any]:
    positions = np.asarray(info.get("obstacle_positions", []), dtype=np.float32)
    velocities = np.asarray(info.get("obstacle_velocities", []), dtype=np.float32)
    ids = np.asarray(info.get("obstacle_ids", []), dtype=np.int64)
    modes = list(info.get("obstacle_motion_modes", []))
    classes = list(info.get("threat_classes", []))
    uav = np.asarray(info.get("uav_position", np.zeros(3)), dtype=np.float32)
    v_cmd = np.asarray(action, dtype=np.float32) * float(env.v_uav_max)
    v_cmd[2] = 0.0
    best = {
        "raw_min_predicted_cpa": float("inf"),
        "raw_min_predicted_ttc": float("nan"),
        "raw_unsafe_action": 0,
        "raw_safe_margin_unsafe_action": 0,
        "raw_unsafe_obstacle_id": -1,
        "raw_unsafe_motion_mode": "none",
        "raw_unsafe_threat_class": "none",
    }
    for slot, pos in enumerate(positions):
        rel = np.asarray(pos[:2] - uav[:2], dtype=np.float32)
        obs_vel = velocities[slot] if slot < len(velocities) else np.zeros(3, dtype=np.float32)
        rel_vel_pred = np.asarray(obs_vel[:2] - v_cmd[:2], dtype=np.float32)
        denom = float(np.dot(rel_vel_pred, rel_vel_pred)) + 1e-8
        tcpa = float(np.clip(-float(np.dot(rel, rel_vel_pred)) / denom, 0.0, args.raw_cpa_horizon))
        cpa = float(np.linalg.norm(rel + rel_vel_pred * tcpa))
        if cpa < float(best["raw_min_predicted_cpa"]):
            best.update(
                {
                    "raw_min_predicted_cpa": cpa,
                    "raw_min_predicted_ttc": tcpa,
                    "raw_unsafe_obstacle_id": int(ids[slot]) if slot < len(ids) else int(slot),
                    "raw_unsafe_motion_mode": str(modes[slot]) if slot < len(modes) else "none",
                    "raw_unsafe_threat_class": str(classes[slot]) if slot < len(classes) else "none",
                }
            )
    if math.isfinite(float(best["raw_min_predicted_cpa"])):
        unsafe = 0.0 < float(best["raw_min_predicted_ttc"]) < args.raw_cpa_horizon and float(best["raw_min_predicted_cpa"]) < args.raw_cpa_threshold
        unsafe_safe = 0.0 < float(best["raw_min_predicted_ttc"]) < args.raw_cpa_horizon and float(best["raw_min_predicted_cpa"]) < args.raw_cpa_safe_threshold
        best["raw_unsafe_action"] = int(unsafe)
        best["raw_safe_margin_unsafe_action"] = int(unsafe_safe)
    else:
        best["raw_min_predicted_cpa"] = float("nan")
    return best


def gpsi_step_summary(info: dict[str, Any], env: GpsiObsWrapper) -> dict[str, Any]:
    debug = env.latest_gpsi_debug
    mask = np.asarray(debug.get("history_valid_ratio", np.zeros(env.max_obs)), dtype=np.float32)
    active_slots = np.asarray(debug.get("active_slots", []), dtype=np.int64)
    delta = np.asarray(debug.get("delta_hat_raw", np.zeros((env.max_obs, 3, 3))), dtype=np.float32)
    logvar = np.asarray(debug.get("logvar_hat", np.zeros((env.max_obs, 3, 3))), dtype=np.float32)
    positions = np.asarray(info.get("obstacle_positions", []), dtype=np.float32)
    uav = np.asarray(info.get("uav_position", np.zeros(3)), dtype=np.float32)
    nearest_slot = -1
    if len(positions):
        distances = np.linalg.norm(positions[:, :3] - uav[:3], axis=1)
        nearest_slot = int(np.argmin(distances))
    row: dict[str, Any] = {
        "gpsi_forward_ms": float(debug.get("gpsi_forward_ms", 0.0)),
        "gpsi_forward_batch_size": int(debug.get("gpsi_forward_batch_size", 0)),
    }
    for horizon_idx, suffix in enumerate(["1s", "2s", "4s"]):
        active_delta = delta[active_slots, horizon_idx, :] if active_slots.size else np.zeros((0, 3), dtype=np.float32)
        active_logvar_xy = logvar[active_slots, horizon_idx, :2] if active_slots.size else np.zeros((0, 2), dtype=np.float32)
        delta_norm = np.linalg.norm(active_delta, axis=1) if active_delta.size else np.asarray([], dtype=np.float32)
        row[f"mean_delta_norm_{suffix}"] = float(np.mean(delta_norm)) if delta_norm.size else np.nan
        row[f"max_delta_norm_{suffix}"] = float(np.max(delta_norm)) if delta_norm.size else np.nan
        row[f"mean_logvar_xy_{suffix}"] = float(np.mean(active_logvar_xy)) if active_logvar_xy.size else np.nan
        row[f"max_logvar_xy_{suffix}"] = float(np.max(active_logvar_xy)) if active_logvar_xy.size else np.nan
        if 0 <= nearest_slot < env.max_obs:
            row[f"nearest_obstacle_delta_norm_{suffix}"] = float(np.linalg.norm(delta[nearest_slot, horizon_idx, :]))
            row[f"nearest_obstacle_logvar_xy_{suffix}"] = float(np.mean(logvar[nearest_slot, horizon_idx, :2]))
        else:
            row[f"nearest_obstacle_delta_norm_{suffix}"] = np.nan
            row[f"nearest_obstacle_logvar_xy_{suffix}"] = np.nan
    if 0 <= nearest_slot < len(positions):
        rel = positions[nearest_slot] - uav
        rel_xy = rel[:2]
        radial = normalize(rel_xy)
        velocities = np.asarray(info.get("obstacle_velocities", []), dtype=np.float32)
        uav_vel = np.asarray(info.get("uav_velocity", np.zeros(3)), dtype=np.float32)
        relvel_xy = velocities[nearest_slot, :2] - uav_vel[:2] if nearest_slot < len(velocities) else np.zeros(2, dtype=np.float32)
        relvel_dir = normalize(relvel_xy)
        sigma2_xy = np.exp(np.clip(logvar[nearest_slot, 0, :2], -5.0, 3.0))
        row["projected_std_radial_nearest"] = float(np.sqrt(np.sum((radial**2) * sigma2_xy)))
        row["projected_std_relvel_nearest"] = float(np.sqrt(np.sum((relvel_dir**2) * sigma2_xy)))
        row["history_valid_ratio_nearest"] = float(mask[nearest_slot]) if nearest_slot < len(mask) else np.nan
    else:
        row["projected_std_radial_nearest"] = np.nan
        row["projected_std_relvel_nearest"] = np.nan
        row["history_valid_ratio_nearest"] = np.nan
    return row


def lateral_deviation(action: np.ndarray, goal: np.ndarray, uav: np.ndarray, v_uav_max: float) -> float:
    goal_vec = goal - uav
    goal_vec[2] = 0.0
    goal_dir = normalize(goal_vec)
    v_des = np.asarray(action, dtype=np.float32) * v_uav_max
    v_des[2] = 0.0
    parallel = np.dot(v_des, goal_dir) * goal_dir
    return float(np.linalg.norm(v_des - parallel))


def evaluate_episode(
    *,
    model: PPO,
    env: DynamicObstacleFlowEnv | GpsiObsWrapper,
    method: str,
    checkpoint_name: str,
    checkpoint_step_value: int,
    scenario: str,
    episode_id: int,
    episode_seed: int,
    args: argparse.Namespace,
) -> tuple[dict[str, Any], list[dict[str, Any]], list[dict[str, Any]], list[dict[str, Any]], list[dict[str, Any]]]:
    obs, info = env.reset(seed=episode_seed)
    done = False
    steps = 0
    episode_reward = 0.0
    min_distance_values: list[float] = []
    raw_rows: list[dict[str, Any]] = []
    gpsi_rows: list[dict[str, Any]] = []
    trace_rows: list[dict[str, Any]] = []
    profile_rows: list[dict[str, Any]] = []
    last_info = info
    raw_unsafe_count = 0

    while not done:
        action, _ = model.predict(obs, deterministic=True)
        action = np.asarray(action, dtype=np.float32)
        obs, reward, terminated, truncated, info = env.step(action)
        done = bool(terminated or truncated)
        steps += 1
        episode_reward += float(reward)
        last_info = info
        base_env = env.unwrapped if hasattr(env, "unwrapped") else env
        if isinstance(env, GpsiObsWrapper):
            flow_env = env.env
        else:
            flow_env = env

        min_distance = float(info["min_distance"])
        min_distance_values.append(min_distance)
        raw = raw_action_diagnostics(action, info, flow_env, args)
        raw_unsafe_count += int(raw["raw_unsafe_action"])
        raw_row = {
            "method": method,
            "checkpoint": checkpoint_name,
            "checkpoint_step": int(checkpoint_step_value),
            "scenario": scenario,
            "episode_id": int(episode_id),
            "step": int(info["step"]),
            "time": float(info["time"]),
            "motion_mode": raw["raw_unsafe_motion_mode"],
            "threat_class": raw["raw_unsafe_threat_class"],
            **raw,
        }
        raw_rows.append(raw_row)

        weights, entropy = attention_snapshot(model, flow_env.max_obs)
        threat_index = int(info.get("threat_obstacle_index", -1))
        threat_weight = float(weights[threat_index]) if 0 <= threat_index < len(weights) else float("nan")
        if isinstance(env, GpsiObsWrapper):
            gpsi_summary = {
                "method": method,
                "checkpoint": checkpoint_name,
                "checkpoint_step": int(checkpoint_step_value),
                "scenario": scenario,
                "episode_id": int(episode_id),
                "step": int(info["step"]),
                "time": float(info["time"]),
                "motion_mode": str(info.get("threat_motion_mode", "none")),
                "threat_class": str(info.get("threat_class", "none")),
                **gpsi_step_summary(info, env),
            }
            gpsi_rows.append(gpsi_summary)
            profile_rows.append(
                {
                    "method": method,
                    "checkpoint": checkpoint_name,
                    "checkpoint_step": int(checkpoint_step_value),
                    "scenario": scenario,
                    "episode_id": int(episode_id),
                    "step": int(info["step"]),
                    "gpsi_forward_ms": float(gpsi_summary["gpsi_forward_ms"]),
                    "gpsi_forward_batch_size": int(gpsi_summary["gpsi_forward_batch_size"]),
                }
            )
        else:
            gpsi_summary = {}

        if args.write_traces:
            uav = np.asarray(info["uav_position"], dtype=np.float32)
            goal = np.asarray(info["goal_position"], dtype=np.float32)
            uav_vel = np.asarray(info["uav_velocity"], dtype=np.float32)
            trace_row = {
                "method": method,
                "checkpoint": checkpoint_name,
                "checkpoint_step": int(checkpoint_step_value),
                "scenario": scenario,
                "episode_id": int(episode_id),
                "step": int(info["step"]),
                "time": float(info["time"]),
                "uav_pos": json_array(uav),
                "uav_vel": json_array(uav_vel),
                "action_raw": json_array(action),
                "action_executed": json_array(action),
                "filter_used": False,
                "filter_triggered": False,
                "filter_delta_norm": 0.0,
                "lateral_deviation": lateral_deviation(action, goal, uav, flow_env.v_uav_max),
                "goal_directed_velocity": float(np.dot(uav_vel, normalize(goal - uav))),
                "threat_obstacle_id": int(info.get("threat_obstacle_id", -1)),
                "threat_obstacle_index": int(threat_index),
                "threat_class": str(info.get("threat_class", "none")),
                "threat_motion_mode": str(info.get("threat_motion_mode", "none")),
                "planned_cpa": float(info.get("planned_cpa_to_threat", np.nan)),
                "planned_ttc": float(info.get("planned_ttc_to_threat", np.nan)),
                "planned_ttc_remaining": float(info.get("planned_ttc_remaining_to_threat", np.nan)),
                "min_distance": min_distance,
                "episode_min_distance_so_far": float(info.get("episode_min_distance", np.nan)),
                "attention_weights": json_array(weights),
                "attention_entropy": entropy,
                "threat_obstacle_attention_weight": threat_weight,
                **raw,
            }
            for key, value in gpsi_summary.items():
                if key not in trace_row and key not in {"method", "checkpoint", "checkpoint_step", "scenario", "episode_id", "step", "time"}:
                    trace_row[key] = value
            trace_rows.append(trace_row)

    near_miss = int(float(last_info["episode_min_distance"]) < args.near_miss_distance)
    row = {
        "method": method,
        "checkpoint": checkpoint_name,
        "checkpoint_step": int(checkpoint_step_value),
        "seed": int(args.seed),
        "scenario": scenario,
        "episode_id": int(episode_id),
        "episode_seed": int(episode_seed),
        "success": int(bool(last_info["is_success"])),
        "collision": int(bool(last_info["is_collision"])),
        "near_miss": near_miss,
        "mean_min_distance": float(np.mean(min_distance_values)) if min_distance_values else np.nan,
        "episode_min_distance": float(last_info["episode_min_distance"]),
        "mean_time": float(steps * float(last_info.get("dt", 0.2))),
        "episode_length": int(steps),
        "progress": float(last_info["progress"]),
        "planned_cpa": float(last_info.get("planned_cpa_to_threat", np.nan)),
        "planned_ttc": float(last_info.get("planned_ttc_to_threat", np.nan)),
        "threat_class": str(last_info.get("threat_class", "none")),
        "threat_motion_mode": str(last_info.get("threat_motion_mode", "none")),
        "threat_valid_rate": float(last_info.get("threat_valid_rate", np.nan)),
        "replacement_count": int(last_info.get("replacement_count", 0)),
        "episode_reward": float(episode_reward),
        "raw_unsafe_action_rate": float(raw_unsafe_count / max(steps, 1)),
        "nan_or_crash": 0,
    }
    return row, trace_rows, raw_rows, gpsi_rows, profile_rows


def summarize(rows: list[dict[str, Any]], group_cols: list[str]) -> list[dict[str, Any]]:
    if not rows:
        return []
    import pandas as pd

    df = pd.DataFrame(rows)
    aggregations: dict[str, Any] = {}
    candidates = {
        "episode_id": ("episodes", "count"),
        "success": ("success_rate", "mean"),
        "collision": ("collision_rate", "mean"),
        "near_miss": ("near_miss_rate", "mean"),
        "mean_min_distance": ("mean_min_distance", "mean"),
        "episode_min_distance": ("episode_min_distance", "mean"),
        "mean_time": ("mean_time", "mean"),
        "episode_length": ("episode_length", "mean"),
        "progress": ("progress", "mean"),
        "episode_reward": ("episode_reward", "mean"),
        "raw_unsafe_action_rate": ("raw_unsafe_action_rate", "mean"),
        "nan_or_crash": ("nan_or_crash", "sum"),
    }
    for source, (target, agg) in candidates.items():
        if source in df.columns:
            aggregations[target] = (source, agg)
    out = df.groupby(group_cols, dropna=False).agg(**aggregations).reset_index()
    return out.to_dict("records")


def summarize_raw(rows: list[dict[str, Any]]) -> list[dict[str, Any]]:
    if not rows:
        return []
    import pandas as pd

    df = pd.DataFrame(rows)
    group_cols = ["method", "checkpoint", "checkpoint_step", "scenario", "motion_mode", "threat_class"]
    out = (
        df.groupby(group_cols, dropna=False)
        .agg(
            steps=("step", "count"),
            raw_unsafe_rate=("raw_unsafe_action", "mean"),
            raw_safe_margin_unsafe_rate=("raw_safe_margin_unsafe_action", "mean"),
            raw_min_predicted_cpa=("raw_min_predicted_cpa", "mean"),
            raw_min_predicted_ttc=("raw_min_predicted_ttc", "mean"),
        )
        .reset_index()
    )
    return out.to_dict("records")


def summarize_gpsi(rows: list[dict[str, Any]]) -> list[dict[str, Any]]:
    if not rows:
        return []
    import pandas as pd

    df = pd.DataFrame(rows)
    group_cols = ["method", "checkpoint", "checkpoint_step", "scenario", "motion_mode", "threat_class"]
    numeric_cols = [
        col
        for col in df.columns
        if col not in group_cols and col not in {"episode_id", "step", "time"} and np.issubdtype(df[col].dtype, np.number)
    ]
    agg = {col: (col, "mean") for col in numeric_cols}
    out = df.groupby(group_cols, dropna=False).agg(**agg).reset_index()
    return out.to_dict("records")


def copy_sample_traces(out_dir: Path, trace_files: list[tuple[Path, dict[str, Any]]]) -> None:
    success = collision = near = unsafe = 0
    for path, row in trace_files:
        if not path.exists():
            continue
        stem = path.name
        if int(row.get("success", 0)) and success < 6:
            shutil.copyfile(path, out_dir / "traces/sampled_success_traces" / stem)
            success += 1
        if int(row.get("collision", 0)) and collision < 6:
            shutil.copyfile(path, out_dir / "traces/sampled_collision_traces" / stem)
            collision += 1
        if int(row.get("near_miss", 0)) and not int(row.get("collision", 0)) and near < 6:
            shutil.copyfile(path, out_dir / "traces/sampled_near_miss_traces" / stem)
            near += 1
        if float(row.get("raw_unsafe_action_rate", 0.0)) >= 0.10 and unsafe < 6:
            shutil.copyfile(path, out_dir / "traces/high_raw_unsafe_rate_traces" / stem)
            unsafe += 1


def evaluate_method_checkpoint(
    *,
    method: str,
    checkpoint_path: Path,
    checkpoint_name: str,
    checkpoint_step_value: int,
    args: argparse.Namespace,
    out_dir: Path,
) -> tuple[list[dict[str, Any]], list[dict[str, Any]], list[dict[str, Any]], list[dict[str, Any]], list[dict[str, Any]], list[tuple[Path, dict[str, Any]]]]:
    if not checkpoint_path.exists():
        raise PhaseN3EvalStop("eval_failed", f"missing checkpoint: {rel(checkpoint_path)}")
    if method == "attention_full":
        model = PPO.load(
            str(checkpoint_path),
            device=args.device,
            custom_objects={"policy_kwargs": policy_kwargs_attention(args.hidden_dim)},
        )
    else:
        model = PPO.load(str(checkpoint_path), device=args.device)

    episode_rows: list[dict[str, Any]] = []
    raw_rows: list[dict[str, Any]] = []
    gpsi_rows: list[dict[str, Any]] = []
    profile_rows: list[dict[str, Any]] = []
    trace_files: list[tuple[Path, dict[str, Any]]] = []

    for scenario in args.scenarios:
        env: DynamicObstacleFlowEnv | GpsiObsWrapper
        env = make_gpsi_env(args, scenario) if method != "attention_full" else DynamicObstacleFlowEnv(scenario=scenario)
        start = time.time()
        last = start
        print(
            f"[{time.strftime('%Y-%m-%d %H:%M:%S')}] N3_EVAL_START "
            f"method={method} checkpoint={checkpoint_name} scenario={scenario} episodes={args.num_episodes}",
            flush=True,
        )
        for episode_id in range(args.num_episodes):
            seed = args.eval_seed + args.seed * 10000 + episode_id
            row, trace_rows, raw_ep, gpsi_ep, profile_ep = evaluate_episode(
                model=model,
                env=env,
                method=method,
                checkpoint_name=checkpoint_name,
                checkpoint_step_value=checkpoint_step_value,
                scenario=scenario,
                episode_id=episode_id,
                episode_seed=seed,
                args=args,
            )
            episode_rows.append(row)
            raw_rows.extend(raw_ep)
            gpsi_rows.extend(gpsi_ep)
            profile_rows.extend(profile_ep)
            if args.write_traces and trace_rows:
                trace_path = out_dir / "traces" / f"{method}_{checkpoint_name}_{scenario}_ep{episode_id}.csv"
                write_csv(trace_path, trace_rows)
                trace_files.append((trace_path, row))
            now = time.time()
            if now - last >= args.heartbeat_seconds or episode_id == args.num_episodes - 1:
                done = episode_id + 1
                rate = done / max(now - start, 1e-6)
                eta = (args.num_episodes - done) / max(rate, 1e-6)
                print(
                    f"[{time.strftime('%Y-%m-%d %H:%M:%S')}] N3_EVAL_HEARTBEAT "
                    f"method={method} checkpoint={checkpoint_name} scenario={scenario} "
                    f"episodes={done}/{args.num_episodes} rate={rate:.2f} ep/s eta={eta/60.0:.2f} min",
                    flush=True,
                )
                last = now
        env.close()
    return episode_rows, [], raw_rows, gpsi_rows, profile_rows, trace_files


def main() -> None:
    args = parse_args()
    out_dir = ROOT / args.out_dir
    ensure_dirs(out_dir)
    try:
        if not (ROOT / args.gpsi_checkpoint).exists():
            raise PhaseN3EvalStop("gpsi_checkpoint_missing", f"missing Gpsi checkpoint: {args.gpsi_checkpoint}")
        append_command_manifest(out_dir, args)
        torch.manual_seed(args.seed)
        np.random.seed(args.seed)
        all_episode_rows: list[dict[str, Any]] = []
        all_raw_rows: list[dict[str, Any]] = []
        all_gpsi_rows: list[dict[str, Any]] = []
        all_profile_rows: list[dict[str, Any]] = []
        all_trace_files: list[tuple[Path, dict[str, Any]]] = []

        attention_path = ROOT / args.attention_checkpoint
        ep, _unused, raw, gpsi, profile, traces = evaluate_method_checkpoint(
            method="attention_full",
            checkpoint_path=attention_path,
            checkpoint_name="attention_full_1500k",
            checkpoint_step_value=1_500_000,
            args=args,
            out_dir=out_dir,
        )
        all_episode_rows.extend(ep)
        all_raw_rows.extend(raw)
        all_gpsi_rows.extend(gpsi)
        all_profile_rows.extend(profile)
        all_trace_files.extend(traces)

        for checkpoint in args.ppo_checkpoints:
            path = ROOT / checkpoint
            step = checkpoint_step(path)
            name = f"gpsi_ppo_{step // 1000}k" if step else path.stem
            ep, _unused, raw, gpsi, profile, traces = evaluate_method_checkpoint(
                method="gpsi_heada_ppo_no_shield",
                checkpoint_path=path,
                checkpoint_name=name,
                checkpoint_step_value=step,
                args=args,
                out_dir=out_dir,
            )
            all_episode_rows.extend(ep)
            all_raw_rows.extend(raw)
            all_gpsi_rows.extend(gpsi)
            all_profile_rows.extend(profile)
            all_trace_files.extend(traces)

        write_csv(out_dir / "tables/phase_n3_episode_metrics.csv", all_episode_rows)
        summary = summarize(all_episode_rows, ["method", "checkpoint", "checkpoint_step", "scenario"])
        write_csv(out_dir / "tables/phase_n3_checkpoint_eval_summary.csv", summary)
        write_csv(out_dir / "tables/phase_n3_eval_summary.csv", summary)
        scenario_summary = summarize(all_episode_rows, ["method", "checkpoint", "checkpoint_step", "scenario"])
        motion_summary = summarize(all_episode_rows, ["method", "checkpoint", "checkpoint_step", "threat_motion_mode"])
        threat_summary = summarize(all_episode_rows, ["method", "checkpoint", "checkpoint_step", "threat_class"])
        write_csv(out_dir / "tables/phase_n3_scenario_breakdown.csv", scenario_summary)
        write_csv(out_dir / "tables/phase_n3_motion_mode_breakdown.csv", motion_summary)
        write_csv(out_dir / "tables/phase_n3_threat_class_breakdown.csv", threat_summary)
        write_csv(out_dir / "tables/phase_n3_raw_unsafe_action_steps.csv", all_raw_rows)
        write_csv(out_dir / "tables/phase_n3_raw_unsafe_action_summary.csv", summarize_raw(all_raw_rows))
        write_csv(out_dir / "tables/phase_n3_gpsi_output_steps.csv", all_gpsi_rows)
        write_csv(out_dir / "tables/phase_n3_gpsi_output_summary.csv", summarize_gpsi(all_gpsi_rows))
        write_csv(out_dir / "tables/phase_n3_gpsi_forward_profile.csv", all_profile_rows)
        copy_sample_traces(out_dir, all_trace_files)
        print(
            f"[{time.strftime('%Y-%m-%d %H:%M:%S')}] N3_EVAL_END "
            f"episodes={len(all_episode_rows)} raw_steps={len(all_raw_rows)} gpsi_steps={len(all_gpsi_rows)}",
            flush=True,
        )
    except PhaseN3EvalStop as exc:
        write_stop(out_dir, exc.reason, exc.detail)
        raise SystemExit(2) from exc
    except Exception:
        detail = traceback.format_exc()
        write_stop(out_dir, "eval_failed", detail)
        raise SystemExit(2)


if __name__ == "__main__":
    main()
