def policy_vector(model: PPO) -> torch.Tensor:
    return torch.cat([p.detach().cpu().float().reshape(-1) for p in model.policy.parameters()])


def write_checkpoint_integrity(args: argparse.Namespace, out_dir: Path) -> None:
    rows: list[dict[str, Any]] = []
    previous: torch.Tensor | None = None
    for label, filename, expected in [
        ("250k", "checkpoint_250k.zip", 250_000),
        ("500k", "checkpoint_500k.zip", 500_000),
        ("750k", "checkpoint_750k.zip", 750_000),
        ("1000k", "checkpoint_1000k.zip", 1_000_000),
        ("1250k", "checkpoint_1250k.zip", 1_250_000),
        ("1500k", "checkpoint_1500k.zip", 1_500_000),
        ("final", "final.zip", 1_500_000),
    ]:
        path = out_dir / filename
        exists = path.exists() and path.stat().st_size > 0
        row: dict[str, Any] = {
            "run_name": args.run_name,
            "training_seed": int(args.seed),
            "checkpoint_label": label,
            "checkpoint_path": rel(path),
            "exists": int(exists),
            "size_bytes": int(path.stat().st_size) if exists else 0,
            "sha256": sha256(path) if exists else "missing",
            "expected_total_steps": expected,
            "model_num_timesteps": np.nan,
            "optimizer_state_present": 0,
            "policy_parameter_l2_norm": np.nan,
            "policy_parameter_delta_vs_previous_checkpoint": np.nan,
        }
        if exists:
            model = PPO.load(str(path), device="cpu")
            params = policy_vector(model)
            row["model_num_timesteps"] = int(model.num_timesteps)
            row["optimizer_state_present"] = int(bool(model.policy.optimizer.state_dict().get("state", {})))
            row["policy_parameter_l2_norm"] = float(torch.linalg.vector_norm(params).item())
            if previous is not None and previous.numel() == params.numel():
                row["policy_parameter_delta_vs_previous_checkpoint"] = float(torch.linalg.vector_norm(params - previous).item())
            previous = params
            del model
        rows.append(row)
    write_csv(STEP_B_DIR / "tables/phase_n3pf_ms_seed2b_checkpoint_integrity.csv", rows)


def train() -> None:
    args = parse_args()
    out_dir = ROOT / args.out_dir
    ensure_dirs(out_dir)
    try:
        cfg = load_config(ROOT / args.config)
        validate_config(cfg, args, out_dir)
        if args.validate_only:
            write_resource_affinity("validate_only", args)
            validate_wrapper(cfg, args)
            print(
                f"[{time.strftime('%Y-%m-%d %H:%M:%S')}] N3PF_MS_AB_VALIDATE_OK "
                f"run={args.run_name} seed={args.seed} out_dir={rel(out_dir)}",
                flush=True,
            )
            return
        action = base_train.prepare_training_guard(out_dir, cfg, args)
        if action in {"skip", "wait"}:
            write_checkpoint_integrity(args, out_dir)
            return
        save_yaml(out_dir / "config_resolved.yaml", cfg)
        write_resource_affinity("train_preflight", args)
        write_command_manifest(args, cfg, out_dir)
        set_seed(int(args.seed))
        validate_wrapper(cfg, args)

        env_fns = [n3p_train.make_env_factory(cfg, args, rank) for rank in range(int(args.n_envs))]
        vec_env = DummyVecEnv(env_fns)
        ppo_cfg = cfg.get("ppo", {})
        method_key = f"p3_s{int(args.seed)}_{args.run_name}"
        method_name = str(cfg.get("method_name", method_key))
        print(
            f"[{time.strftime('%Y-%m-%d %H:%M:%S')}] N3PF_MS_AB_TRAIN_CONFIG "
            f"run={args.run_name} seed={args.seed} steps={args.train_steps} n_envs={args.n_envs} device={args.device} "
            f"out_dir={rel(out_dir)} no_shield=true",
            flush=True,
        )
        model = PPO(
            "MultiInputPolicy",
            vec_env,
            seed=int(args.seed),
            device=args.device,
            verbose=1,
            tensorboard_log=str(out_dir / "tensorboard"),
            policy_kwargs=n3p_train.make_policy_kwargs(cfg),
            n_steps=int(ppo_cfg.get("n_steps", 1024)),
            batch_size=int(ppo_cfg.get("batch_size", 256)),
            n_epochs=int(ppo_cfg.get("n_epochs", 10)),
            learning_rate=float(ppo_cfg.get("learning_rate", 3e-4)),
            gamma=float(ppo_cfg.get("gamma", 0.99)),
            gae_lambda=float(ppo_cfg.get("gae_lambda", 0.95)),
            clip_range=float(ppo_cfg.get("clip_range", 0.2)),
            ent_coef=float(ppo_cfg.get("ent_coef", 0.01)),
            vf_coef=float(ppo_cfg.get("vf_coef", 0.5)),
            max_grad_norm=float(ppo_cfg.get("max_grad_norm", 0.5)),
        )
        write_config_manifest(cfg, args, out_dir, n3p_train.adapter_parameter_count(model))
        callback = base_train.N3FZTrainCallback(int(args.train_steps), method_key, method_name, float(args.heartbeat_seconds))
        checkpoint_cb = base_train.FixedNameCheckpointCallback([int(step) for step in args.checkpoint_steps], out_dir)
        model.learn(total_timesteps=int(args.train_steps), callback=[callback, checkpoint_cb], progress_bar=False, reset_num_timesteps=True)
        final_path = out_dir / "final.zip"
        model.save(str(final_path))
        final_step_checkpoint = out_dir / f"checkpoint_{int(args.train_steps) // 1000}k.zip"
        if not final_step_checkpoint.exists():
            shutil.copyfile(final_path, final_step_checkpoint)
        episode_rows = [dict(row, run_name=args.run_name, training_seed=int(args.seed)) for row in callback.episode_rows]
        heartbeat_rows = [dict(row, run_name=args.run_name, training_seed=int(args.seed)) for row in callback.heartbeat_rows]
        append_csv(STEP_B_DIR / "tables/phase_n3pf_ms_seed2b_train_curve.csv", episode_rows)
        append_csv(STEP_B_DIR / "tables/phase_n3pf_ms_seed2b_train_heartbeat.csv", heartbeat_rows)
        write_checkpoint_integrity(args, out_dir)
        vec_env.close()
        base_train.complete_path(out_dir).write_text(f"completed\ntrain_steps={int(args.train_steps)}\ntraining_seed={int(args.seed)}\nrun_name={args.run_name}\n", encoding="utf-8")
        base_train.write_train_status(out_dir, "completed", cfg, args, "training completed successfully")
        base_train.lock_path(out_dir).unlink(missing_ok=True)
        print(f"[{time.strftime('%Y-%m-%d %H:%M:%S')}] N3PF_MS_AB_TRAIN_END run={args.run_name} seed={args.seed} final={rel(final_path)}", flush=True)
    except Seed2ABTrainStop as exc:
        write_stop(exc.reason, exc.detail)
        raise SystemExit(2) from exc
    except base_train.PhaseN3FZTrainStop as exc:
        write_stop("train_failed", exc.detail)
        raise SystemExit(2) from exc
    except Exception:
        detail = traceback.format_exc()
        try:
            if "cfg" in locals():
                base_train.write_train_status(out_dir, "failed", cfg, args, detail)
            base_train.lock_path(out_dir).unlink(missing_ok=True)
        except Exception:
            pass
        write_stop("train_failed", detail)
        raise SystemExit(2)


if __name__ == "__main__":
    train()
