class Seed2ABTrainStop(Exception):
    def __init__(self, reason: str, detail: str) -> None:
        super().__init__(detail)
        self.reason = reason
        self.detail = detail


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Phase N3PF-MS-AB Step B P3 seed rerun training.")
    parser.add_argument("--config", default="configs/env_v2_gpsi_heada_ppo_n3pf_block_projected.yaml")
    parser.add_argument("--out-dir", required=True)
    parser.add_argument("--run-name", choices=["seed2_rerunA", "seed3_sanity"], required=True)
    parser.add_argument("--train-steps", type=int, default=1_500_000)
    parser.add_argument("--checkpoint-steps", nargs="*", type=int, default=CHECKPOINT_STEPS)
    parser.add_argument("--seed", type=int, required=True)
    parser.add_argument("--n-envs", type=int, default=4)
    parser.add_argument("--device", default="cpu")
    parser.add_argument("--heartbeat-seconds", type=float, default=300.0)
    parser.add_argument("--validate-only", action="store_true")
    return parser.parse_args()


def rel(path: Path) -> str:
    try:
        return str(path.resolve().relative_to(ROOT))
    except ValueError:
        return str(path)


def ensure_dirs(out_dir: Path) -> None:
    for path in [out_dir, RESULT_DIR, STEP_B_DIR, STEP_B_DIR / "logs", STEP_B_DIR / "tables", STEP_B_DIR / "plots"]:
        path.mkdir(parents=True, exist_ok=True)


def write_text(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


def write_csv(path: Path, rows: list[dict[str, Any]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    if not rows:
        path.write_text("", encoding="utf-8")
        return
    fields: list[str] = []
    seen: set[str] = set()
    for row in rows:
        for key in row:
            if key not in seen:
                fields.append(key)
                seen.add(key)
    with path.open("w", newline="", encoding="utf-8") as handle:
        writer = csv.DictWriter(handle, fieldnames=fields, extrasaction="ignore")
        writer.writeheader()
        writer.writerows(rows)


def append_csv(path: Path, rows: list[dict[str, Any]]) -> None:
    old: list[dict[str, Any]] = []
    if path.exists() and path.stat().st_size > 0:
        with path.open("r", newline="", encoding="utf-8") as handle:
            old = list(csv.DictReader(handle))
    write_csv(path, old + rows)


def write_stop(reason: str, detail: str) -> None:
    RESULT_DIR.mkdir(parents=True, exist_ok=True)
    flag = STOP_FLAGS.get(reason, STOP_FLAGS["train_failed"])
    write_text(RESULT_DIR / flag, f"{reason}\n{detail.strip()}\n")
    write_text(RESULT_DIR / "phase_n3pf_ms_seed2_ab_status.txt", f"stopped:{flag}\n")
    write_text(
        RESULT_DIR / "PHASE_N3PF_MS_SEED2_AB_AUDIT_RERUN_REPORT.md",
        "\n".join(
            [
                "# Phase N3PF-MS-AB Seed2 Collapse Audit + Minimal Rerun Sanity",
                "",
                f"`terminal_decision = phase_n3pf_ms_seed2_ab_stopped_{reason}`",
                "",
                "Partial report generated by Step B training.",
                "",
                "```text",
                detail.strip(),
                "```",
            ]
        )
        + "\n",
    )


def load_config(path: Path) -> dict[str, Any]:
    with path.open("r", encoding="utf-8") as handle:
        payload = yaml.safe_load(handle)
    if not isinstance(payload, dict):
        raise Seed2ABTrainStop("config_mismatch", f"config is not a mapping: {rel(path)}")
    return payload


def save_yaml(path: Path, payload: dict[str, Any]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(yaml.safe_dump(payload, sort_keys=False), encoding="utf-8")


def sha256(path: Path) -> str:
    h = hashlib.sha256()
    with path.open("rb") as handle:
        for chunk in iter(lambda: handle.read(1024 * 1024), b""):
            h.update(chunk)
    return h.hexdigest()


def run_command(label: str, command: list[str]) -> dict[str, Any]:
    try:
        proc = subprocess.run(command, check=False, text=True, capture_output=True, timeout=30)
        return {"item": label, "command": " ".join(command), "returncode": int(proc.returncode), "output": (proc.stdout + proc.stderr).strip()}
    except Exception as exc:
        return {"item": label, "command": " ".join(command), "returncode": -1, "output": f"{type(exc).__name__}: {exc}"}


def write_resource_affinity(stage: str, args: argparse.Namespace) -> None:
    rows = [
        run_command("nproc", ["nproc"]),
        run_command("nproc_all", ["nproc", "--all"]),
        run_command("taskset_current_process", ["taskset", "-pc", str(os.getpid())]),
        run_command("lscpu", ["lscpu"]),
        run_command("free_h", ["free", "-h"]),
        run_command("df_root", ["df", "-h", "/"]),
        run_command("nvidia_smi", ["nvidia-smi"]),
    ]
    for path in ["/sys/fs/cgroup/cpuset.cpus.effective", "/sys/fs/cgroup/cpu.max"]:
        p = Path(path)
        rows.append({"item": p.name, "command": f"read {path}", "returncode": 0 if p.exists() else 1, "output": p.read_text().strip() if p.exists() else "missing"})
    rows.append(
        {
            "item": "python_cpu_affinity",
            "command": "python runtime",
            "returncode": 0,
            "output": json.dumps(
                {
                    "os_cpu_count": os.cpu_count(),
                    "sched_affinity_count": len(os.sched_getaffinity(0)) if hasattr(os, "sched_getaffinity") else None,
                    "sched_affinity": sorted(os.sched_getaffinity(0)) if hasattr(os, "sched_getaffinity") else None,
                    "OMP_NUM_THREADS": os.environ.get("OMP_NUM_THREADS", ""),
                    "MKL_NUM_THREADS": os.environ.get("MKL_NUM_THREADS", ""),
                    "OPENBLAS_NUM_THREADS": os.environ.get("OPENBLAS_NUM_THREADS", ""),
                    "NUMEXPR_NUM_THREADS": os.environ.get("NUMEXPR_NUM_THREADS", ""),
                    "CUDA_VISIBLE_DEVICES": os.environ.get("CUDA_VISIBLE_DEVICES", ""),
                },
                sort_keys=True,
            ),
        }
    )
    for row in rows:
        row["stage"] = stage
        row["run_name"] = args.run_name
        row["training_seed"] = int(args.seed)
        row["timestamp"] = time.strftime("%Y-%m-%d %H:%M:%S")
    append_csv(STEP_B_DIR / "tables/phase_n3pf_ms_seed2b_resource_affinity.csv", rows)


def set_seed(seed: int) -> None:
    random.seed(seed)
    np.random.seed(seed)
    torch.manual_seed(seed)
    if torch.cuda.is_available():
        torch.cuda.manual_seed_all(seed)


def validate_config(cfg: dict[str, Any], args: argparse.Namespace, out_dir: Path) -> None:
    if not N3PF_MS_FLAG.exists():
        raise Seed2ABTrainStop("config_mismatch", f"missing prerequisite flag: {rel(N3PF_MS_FLAG)}")
    if out_dir.resolve() in [
        (ROOT / "checkpoints/env_v2_gpsi_heada_ppo_n3pf_ms_block_projected_s1").resolve(),
        (ROOT / "checkpoints/env_v2_gpsi_heada_ppo_n3pf_ms_block_projected_s2").resolve(),
    ]:
        raise Seed2ABTrainStop("config_mismatch", f"refusing to write original N3PF-MS checkpoint dir: {rel(out_dir)}")
    if args.run_name == "seed2_rerunA" and "s2_rerunA" not in out_dir.name:
        raise Seed2ABTrainStop("config_mismatch", f"seed2_rerunA out-dir must be isolated and include s2_rerunA: {rel(out_dir)}")
    if args.run_name == "seed3_sanity" and "s3_sanity" not in out_dir.name:
        raise Seed2ABTrainStop("config_mismatch", f"seed3_sanity out-dir must be isolated and include s3_sanity: {rel(out_dir)}")
    if int(args.train_steps) != 1_500_000:
        raise Seed2ABTrainStop("config_mismatch", f"Step B train_steps must be 1500000, got {args.train_steps}")
    if set(int(step) for step in args.checkpoint_steps) != set(CHECKPOINT_STEPS):
        raise Seed2ABTrainStop("config_mismatch", f"checkpoint steps must be {CHECKPOINT_STEPS}")
    if int(args.n_envs) != 4 or str(args.device) != "cpu":
        raise Seed2ABTrainStop("config_mismatch", f"Step B requires n_envs=4 and device=cpu, got n_envs={args.n_envs} device={args.device}")
    if args.run_name == "seed2_rerunA" and int(args.seed) != 2:
        raise Seed2ABTrainStop("config_mismatch", f"seed2_rerunA must use seed=2, got {args.seed}")
    if args.run_name == "seed3_sanity" and int(args.seed) != 3:
        raise Seed2ABTrainStop("config_mismatch", f"seed3_sanity must use seed=3, got {args.seed}")

    cfg["method_key"] = "block_projected"
    cfg["method_name"] = f"n3pf_ms_ab_block_projected_{args.run_name}"
    cfg.setdefault("env", {})["n_envs"] = int(args.n_envs)
    cfg.setdefault("env", {})["device"] = str(args.device)
    cfg.setdefault("training", {})["seed"] = int(args.seed)
    cfg["training"]["total_steps"] = int(args.train_steps)
    cfg["training"]["checkpoint_steps"] = [int(step) for step in args.checkpoint_steps]
    for key in ["parent_total_steps", "additional_steps", "resume", "reset_num_timesteps"]:
        cfg["training"].pop(key, None)
    cfg["training"]["no_shield"] = True
    cfg["training"]["action_filtering"] = False
    cfg["training"]["use_safety_cost"] = False
    n3p_train.validate_config_semantics(cfg)
    checkpoint = ROOT / str(cfg.get("gpsi", {}).get("checkpoint", "work_dirs/gpsi_heada_v1_nll/best.pth"))
    if not checkpoint.exists():
        raise Seed2ABTrainStop("gpsi_checkpoint_missing", f"missing Gpsi checkpoint: {rel(checkpoint)}")


def validate_wrapper(cfg: dict[str, Any], args: argparse.Namespace) -> None:
    env = n3p_train.make_wrapper(cfg, "eval_flow_id", args.device)
    try:
        obs, _info = env.reset(seed=int(args.seed))
        freeze = env.freeze_check
        expected = int(cfg.get("gpsi", {}).get("obs_aug_dim", 30))
        actual = int(obs["obs"].shape[-1])
        rows = [
            {"run_name": args.run_name, "training_seed": int(args.seed), "check": "gpsi_frozen", "value": int(not freeze.training and not freeze.requires_grad_any and freeze.trainable_parameters == 0), "detail": json.dumps(freeze.__dict__, sort_keys=True)},
            {"run_name": args.run_name, "training_seed": int(args.seed), "check": "aug_obs_dim", "value": actual, "detail": f"expected={expected}"},
            {"run_name": args.run_name, "training_seed": int(args.seed), "check": "include_z", "value": int(bool(env.latest_gpsi_debug.get("include_z", False))), "detail": "must be 0"},
            {"run_name": args.run_name, "training_seed": int(args.seed), "check": "feature_adapter", "value": str(cfg.get("ppo", {}).get("feature_adapter", "")), "detail": "must be block_projected_no_z"},
        ]
        append_csv(STEP_B_DIR / "tables/phase_n3pf_ms_seed2b_schema_check.csv", rows)
        if freeze.training or freeze.requires_grad_any or freeze.trainable_parameters != 0:
            raise Seed2ABTrainStop("config_mismatch", f"Gpsi freeze check failed: {freeze}")
        if actual != expected:
            raise Seed2ABTrainStop("config_mismatch", f"aug obs dim mismatch: {actual} vs {expected}")
        if bool(env.latest_gpsi_debug.get("include_z", False)):
            raise Seed2ABTrainStop("config_mismatch", "include_z must be false")
    finally:
        env.close()


