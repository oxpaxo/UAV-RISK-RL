class N3PFMSEvalStop(Exception):
    def __init__(self, reason: str, detail: str) -> None:
        super().__init__(detail)
        self.reason = reason
        self.detail = detail


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Evaluate Phase N3PF-MS P3 block-projected multi-training-seed checkpoints.")
    parser.add_argument("--result-dir", default="results/env_v2_phase_n3pf_ms_multiseed")
    parser.add_argument("--training-seeds", nargs="+", type=int, default=[1, 2])
    parser.add_argument("--eval-seeds", nargs="+", type=int, default=[1000, 1001, 1002])
    parser.add_argument("--num-episodes", type=int, default=50)
    parser.add_argument("--scenarios", nargs="+", default=SCENARIOS)
    parser.add_argument("--device", default="cpu")
    parser.add_argument("--write-traces", action="store_true")
    parser.add_argument("--near-miss-distance", type=float, default=1.5)
    parser.add_argument("--raw-cpa-horizon", type=float, default=4.5)
    parser.add_argument("--raw-cpa-threshold", type=float, default=1.2)
    parser.add_argument("--raw-cpa-safe-threshold", type=float, default=1.5)
    parser.add_argument("--no-response-action-norm", type=float, default=0.05)
    parser.add_argument("--heartbeat-seconds", type=float, default=300.0)
    return parser.parse_args()


def rel(path: Path) -> str:
    try:
        return str(path.resolve().relative_to(ROOT))
    except ValueError:
        return str(path)


def ensure_dirs(result_dir: Path) -> None:
    for path in [result_dir, result_dir / "logs", result_dir / "tables", result_dir / "plots", result_dir / "traces"]:
        path.mkdir(parents=True, exist_ok=True)


def write_text(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


def write_csv(path: Path, rows: list[dict[str, Any]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    if not rows:
        path.write_text("", encoding="utf-8")
        return
    fields: list[str] = []
    seen: set[str] = set()
    for row in rows:
        for key in row:
            if key not in seen:
                fields.append(key)
                seen.add(key)
    with path.open("w", newline="", encoding="utf-8") as handle:
        writer = csv.DictWriter(handle, fieldnames=fields, extrasaction="ignore")
        writer.writeheader()
        writer.writerows(rows)


def append_csv(path: Path, rows: list[dict[str, Any]]) -> None:
    old: list[dict[str, Any]] = []
    if path.exists() and path.stat().st_size > 0:
        with path.open("r", newline="", encoding="utf-8") as handle:
            old = list(csv.DictReader(handle))
    write_csv(path, old + rows)


def write_stop(result_dir: Path, reason: str, detail: str) -> None:
    ensure_dirs(result_dir)
    flag = STOP_FLAGS.get(reason, STOP_FLAGS["eval_failed"])
    write_text(result_dir / flag, f"{reason}\n{detail.strip()}\n")
    write_text(result_dir / "phase_n3pf_ms_status.txt", f"stopped:{flag}\n")
    write_text(
        result_dir / "PHASE_N3PF_MS_MULTI_SEED_REPORT.md",
        "\n".join(
            [
                "# Phase N3PF-MS Multi-Training-Seed Report",
                "",
                f"`terminal_decision = phase_n3pf_ms_stopped_{reason}`",
                "",
                "Partial report generated by eval script.",
                "",
                "```text",
                detail.strip(),
                "```",
            ]
        )
        + "\n",
    )


def sha256(path: Path) -> str:
    digest = hashlib.sha256()
    with path.open("rb") as handle:
        for chunk in iter(lambda: handle.read(1024 * 1024), b""):
            digest.update(chunk)
    return digest.hexdigest()


def load_yaml(path: Path) -> dict[str, Any]:
    with path.open("r", encoding="utf-8") as handle:
        payload = yaml.safe_load(handle)
    if not isinstance(payload, dict):
        raise N3PFMSEvalStop("config_mismatch", f"config is not a mapping: {rel(path)}")
    return payload


def checkpoint_specs(training_seed: int) -> list[dict[str, Any]]:
    root = Path(f"checkpoints/env_v2_gpsi_heada_ppo_n3pf_ms_block_projected_s{training_seed}")
    cfg_path = Path("configs/env_v2_gpsi_heada_ppo_n3pf_block_projected.yaml")
    specs = []
    for label, filename, step in [
        ("1000k", "checkpoint_1000k.zip", 1_000_000),
        ("1500k", "checkpoint_1500k.zip", 1_500_000),
        ("final", "final.zip", 1_500_000),
    ]:
        key = f"p3_s{training_seed}_{label}"
        specs.append(
            {
                "method_key": key,
                "method": f"P3_block_projected_seed{training_seed}_{label}",
                "checkpoint_label": label,
                "checkpoint_step": step,
                "checkpoint_path": ROOT / root / filename,
                "checkpoint_dir": ROOT / root,
                "config_path": ROOT / cfg_path,
                "training_seed": int(training_seed),
                "source_phase": "N3PF-MS",
            }
        )
    return specs


def write_checkpoint_manifest(result_dir: Path, specs: list[dict[str, Any]]) -> None:
    rows = []
    for spec in specs:
        path = Path(spec["checkpoint_path"])
        exists = path.exists() and path.stat().st_size > 0
        model_steps = np.nan
        if exists:
            try:
                model = PPO.load(str(path), device="cpu")
                model_steps = int(model.num_timesteps)
                del model
            except Exception:
                model_steps = np.nan
        rows.append(
            {
                "training_seed": int(spec["training_seed"]),
                "method_key": spec["method_key"],
                "checkpoint_label": spec["checkpoint_label"],
                "checkpoint_path": rel(path),
                "exists": int(exists),
                "size_bytes": int(path.stat().st_size) if exists else 0,
                "sha256": sha256(path) if exists else "missing",
                "model_num_timesteps": model_steps,
                "checkpoint_step": int(spec["checkpoint_step"]),
                "source_phase": spec["source_phase"],
            }
        )
    write_csv(result_dir / "tables/phase_n3pf_ms_checkpoint_integrity.csv", rows)
    missing = [row for row in rows if int(row["exists"]) != 1]
    if missing:
        raise N3PFMSEvalStop("eval_failed", "missing N3PF-MS checkpoints:\n" + pd.DataFrame(missing).to_string(index=False))


def register_default_configs(specs: list[dict[str, Any]]) -> None:
    for spec in specs:
        n3p_eval.DEFAULT_CONFIGS[str(spec["method_key"])] = {
            "config": rel(Path(spec["config_path"])),
            "checkpoint_dir": rel(Path(spec["checkpoint_dir"])),
        }


def runtime_cfg(spec: dict[str, Any]) -> dict[str, Any]:
    cfg = load_yaml(Path(spec["config_path"]))
    cfg["method_key"] = "block_projected"
    cfg["method_name"] = str(spec["method"])
    cfg.setdefault("training", {})["seed"] = int(spec["training_seed"])
    return cfg


def eval_args_for(args: argparse.Namespace, training_seed: int) -> SimpleNamespace:
    return SimpleNamespace(
        result_dir=args.result_dir,
        seed=int(training_seed),
        device=args.device,
        write_traces=bool(args.write_traces),
        near_miss_distance=float(args.near_miss_distance),
        raw_cpa_horizon=float(args.raw_cpa_horizon),
        raw_cpa_threshold=float(args.raw_cpa_threshold),
        raw_cpa_safe_threshold=float(args.raw_cpa_safe_threshold),
        no_response_action_norm=float(args.no_response_action_norm),
    )


def imported_reference_tables() -> dict[str, pd.DataFrame]:
    tables = {}
    for name in [
        "phase_n3pfv_episode_metrics.csv",
        "phase_n3pfv_eval_summary_by_seed.csv",
        "phase_n3pfv_scenario_breakdown.csv",
        "phase_n3pfv_motion_mode_breakdown.csv",
        "phase_n3pfv_threat_class_breakdown.csv",
        "phase_n3pfv_raw_unsafe_action_summary.csv",
        "phase_n3pfv_gpsi_output_summary.csv",
        "phase_n3pfv_feature_block_stats.csv",
    ]:
        path = N3PFV_DIR / "tables" / name
        if not path.exists():
            raise N3PFMSEvalStop("eval_failed", f"missing N3PF-V reference table: {rel(path)}")
        tables[name] = pd.read_csv(path)
    return tables


def remap_n3pfv(df: pd.DataFrame) -> pd.DataFrame:
    if "method_key" not in df.columns:
        return df
    mapping = {
        "p3_1000k": ("p3_s0_1000k", 0),
        "p3_1500k": ("p3_s0_1500k", 0),
        "p3_final": ("p3_s0_final", 0),
        "attention_full": ("attention_full", -1),
        "no_z_full": ("no_z_full", -1),
    }
    use = df[df["method_key"].astype(str).isin(mapping)].copy()
    if use.empty:
        return use
    use["source_phase"] = use.get("source_phase", "N3PF-V")
    use["training_seed"] = use["method_key"].astype(str).map(lambda x: mapping[x][1])
    use["method_key"] = use["method_key"].astype(str).map(lambda x: mapping[x][0])
    return use


def summarize_raw_by_seed(rows: list[dict[str, Any]]) -> list[dict[str, Any]]:
    if not rows:
        return []
    df = pd.DataFrame(rows)
    group_cols = ["training_seed", "method_key", "method", "checkpoint", "checkpoint_path", "checkpoint_step", "checkpoint_label", "eval_seed", "scenario", "motion_mode", "threat_class"]
    out = (
        df.groupby(group_cols, dropna=False)
        .agg(
            steps=("step", "count"),
            raw_unsafe_rate=("raw_unsafe_action", "mean"),
            raw_safe_margin_unsafe_rate=("raw_safe_margin_unsafe_action", "mean"),
            raw_min_predicted_cpa=("raw_min_predicted_cpa", "mean"),
            raw_min_predicted_ttc=("raw_min_predicted_ttc", "mean"),
            action_norm=("action_norm", "mean"),
            action_delta=("action_delta", "mean"),
            no_response_rate=("no_response", "mean"),
        )
        .reset_index()
    )
    return out.to_dict("records")


def feature_summary_rows(feature_accum: dict[tuple[Any, ...], dict[str, base_eval.BlockAccumulator]], cfg_by_method: dict[str, dict[str, Any]]) -> list[dict[str, Any]]:
    rows: list[dict[str, Any]] = []
    blocks = ["obs_i_12", "delta_hat_9_after_scale", "logvar_raw_9_clamped", "logvar_scaled_9_policy", "adapter_output_64", "full_aug_obs"]
    for key, groups in feature_accum.items():
        method_key, method, checkpoint, step, label, scenario, motion_mode, threat_class = key
        cfg = cfg_by_method.get(str(method_key), {})
        prefix = {
            "training_seed": int(str(method_key).split("_")[1][1:]) if str(method_key).startswith("p3_s") else -1,
            "method_key": method_key,
            "method": method,
            "checkpoint": checkpoint,
            "checkpoint_step": int(step),
            "checkpoint_label": label,
            "scenario": scenario,
            "motion_mode": motion_mode,
            "threat_class": threat_class,
            "include_z": 0,
            "include_logvar": int(bool(cfg.get("gpsi", {}).get("include_logvar", True))),
            "logvar_output_scale": float(cfg.get("gpsi", {}).get("logvar_output_scale", 1.0)),
        }
        for block in blocks:
            if block in groups:
                rows.append(groups[block].row(prefix, block))
            else:
                rows.append(base_eval.BlockAccumulator().row(prefix, block, not_applicable=True))
    return rows


def main() -> None:
    args = parse_args()
    result_dir = ROOT / args.result_dir
    ensure_dirs(result_dir)
    try:
        specs: list[dict[str, Any]] = []
        for seed in args.training_seeds:
            specs.extend(checkpoint_specs(int(seed)))
        write_checkpoint_manifest(result_dir, specs)
        register_default_configs(specs)
        append_csv(
            result_dir / "tables/phase_n3pf_ms_command_manifest.csv",
            [
                {
                    "stage": "eval",
                    "command": " ".join(["python", *sys.argv]),
                    "training_seeds": json.dumps([int(seed) for seed in args.training_seeds]),
                    "eval_seeds": json.dumps([int(seed) for seed in args.eval_seeds]),
                    "num_episodes": int(args.num_episodes),
                    "scenarios": json.dumps(args.scenarios),
                    "device": args.device,
                    "write_traces": int(bool(args.write_traces)),
                }
            ],
        )
        torch.manual_seed(0)
        np.random.seed(0)
        all_episode_rows: list[dict[str, Any]] = []
        all_raw_rows: list[dict[str, Any]] = []
        all_trace_files: list[tuple[Path, dict[str, Any]]] = []
        gpsi_accum: dict[tuple[Any, ...], dict[str, base_eval.ScalarAccumulator]] = {}
        feature_accum: dict[tuple[Any, ...], dict[str, base_eval.BlockAccumulator]] = {}
        cfg_by_method: dict[str, dict[str, Any]] = {}

        for spec in specs:
            cfg = runtime_cfg(spec)
            cfg_by_method[str(spec["method_key"])] = cfg
            eval_args = eval_args_for(args, int(spec["training_seed"]))
            model = PPO.load(str(spec["checkpoint_path"]), device=args.device)
            for eval_seed in args.eval_seeds:
                for scenario in args.scenarios:
                    env = n3p_eval.make_gpsi_env(cfg, scenario, eval_args)
                    start = time.time()
                    last = start
                    print(
                        f"[{time.strftime('%Y-%m-%d %H:%M:%S')}] N3PF_MS_EVAL_START "
                        f"policy={spec['method_key']} training_seed={spec['training_seed']} eval_seed={eval_seed} "
                        f"scenario={scenario} episodes={args.num_episodes}",
                        flush=True,
                    )
                    for episode_id in range(int(args.num_episodes)):
                        episode_seed = int(eval_seed) + episode_id
                        ep_row, raw_rows, _trace_rows, trace_files = n3p_eval.evaluate_episode(
                            model=model,
                            env=env,
                            cfg=cfg,
                            method_key=str(spec["method_key"]),
                            method=str(spec["method"]),
                            checkpoint_name=str(spec["method_key"]),
                            checkpoint_step_value=int(spec["checkpoint_step"]),
                            checkpoint_label=str(spec["checkpoint_label"]),
                            scenario=scenario,
                            episode_id=(int(eval_seed) - min(args.eval_seeds)) * int(args.num_episodes) + episode_id,
                            episode_seed=episode_seed,
                            args=eval_args,
                            gpsi_accum=gpsi_accum,
                            feature_accum=feature_accum,
                        )
                        ep_row["training_seed"] = int(spec["training_seed"])
                        ep_row["eval_seed"] = int(eval_seed)
                        ep_row["checkpoint_path"] = rel(Path(spec["checkpoint_path"]))
                        ep_row["source_phase"] = spec["source_phase"]
                        for row in raw_rows:
                            row["training_seed"] = int(spec["training_seed"])
                            row["eval_seed"] = int(eval_seed)
                            row["checkpoint_path"] = rel(Path(spec["checkpoint_path"]))
                            row["source_phase"] = spec["source_phase"]
                        all_episode_rows.append(ep_row)
                        all_raw_rows.extend(raw_rows)
                        all_trace_files.extend(trace_files)
                        now = time.time()
                        if now - last >= float(args.heartbeat_seconds) or episode_id == int(args.num_episodes) - 1:
                            done = episode_id + 1
                            rate = done / max(now - start, 1e-6)
                            print(
                                f"[{time.strftime('%Y-%m-%d %H:%M:%S')}] N3PF_MS_EVAL_HEARTBEAT "
                                f"policy={spec['method_key']} eval_seed={eval_seed} scenario={scenario} "
                                f"episodes={done}/{args.num_episodes} rate={rate:.2f} ep/s",
                                flush=True,
                            )
                            last = now
                    env.close()

        by_seed_cols = ["training_seed", "method_key", "method", "checkpoint", "checkpoint_path", "checkpoint_step", "checkpoint_label", "eval_seed"]
        scenario_cols = by_seed_cols + ["scenario"]
        eval_rows = all_episode_rows
        by_seed = base_eval.summarize_episode_rows(eval_rows, by_seed_cols)
        scenario_rows = base_eval.summarize_episode_rows(eval_rows, scenario_cols)
        motion_rows = base_eval.summarize_episode_rows(eval_rows, by_seed_cols + ["threat_motion_mode"])
        threat_rows = base_eval.summarize_episode_rows(eval_rows, by_seed_cols + ["threat_class"])
        raw_summary = summarize_raw_by_seed(all_raw_rows)
        gpsi_rows = base_eval.gpsi_summary_rows(gpsi_accum)
        feature_rows = feature_summary_rows(feature_accum, cfg_by_method)

        refs = imported_reference_tables()
        imported_episode = remap_n3pfv(refs["phase_n3pfv_episode_metrics.csv"])
        imported_by_seed = remap_n3pfv(refs["phase_n3pfv_eval_summary_by_seed.csv"])
        imported_scenario = remap_n3pfv(refs["phase_n3pfv_scenario_breakdown.csv"])
        imported_motion = remap_n3pfv(refs["phase_n3pfv_motion_mode_breakdown.csv"])
        imported_threat = remap_n3pfv(refs["phase_n3pfv_threat_class_breakdown.csv"])
        imported_raw = remap_n3pfv(refs["phase_n3pfv_raw_unsafe_action_summary.csv"])
        imported_gpsi = remap_n3pfv(refs["phase_n3pfv_gpsi_output_summary.csv"])
        imported_feature = remap_n3pfv(refs["phase_n3pfv_feature_block_stats.csv"])

        write_csv(result_dir / "tables/phase_n3pf_ms_episode_metrics.csv", pd.concat([pd.DataFrame(eval_rows), imported_episode], ignore_index=True, sort=False).to_dict("records"))
        write_csv(result_dir / "tables/phase_n3pf_ms_eval_summary_by_seed.csv", pd.concat([pd.DataFrame(by_seed), imported_by_seed], ignore_index=True, sort=False).to_dict("records"))
        write_csv(result_dir / "tables/phase_n3pf_ms_scenario_breakdown.csv", pd.concat([pd.DataFrame(scenario_rows), imported_scenario], ignore_index=True, sort=False).to_dict("records"))
        write_csv(result_dir / "tables/phase_n3pf_ms_motion_mode_breakdown.csv", pd.concat([pd.DataFrame(motion_rows), imported_motion], ignore_index=True, sort=False).to_dict("records"))
        write_csv(result_dir / "tables/phase_n3pf_ms_threat_class_breakdown.csv", pd.concat([pd.DataFrame(threat_rows), imported_threat], ignore_index=True, sort=False).to_dict("records"))
        write_csv(result_dir / "tables/phase_n3pf_ms_raw_unsafe_action_steps.csv", all_raw_rows)
        write_csv(result_dir / "tables/phase_n3pf_ms_raw_unsafe_action_summary.csv", pd.concat([pd.DataFrame(raw_summary), imported_raw], ignore_index=True, sort=False).to_dict("records"))
        write_csv(result_dir / "tables/phase_n3pf_ms_gpsi_output_summary.csv", pd.concat([pd.DataFrame(gpsi_rows), imported_gpsi], ignore_index=True, sort=False).to_dict("records"))
        write_csv(result_dir / "tables/phase_n3pf_ms_feature_block_stats.csv", pd.concat([pd.DataFrame(feature_rows), imported_feature], ignore_index=True, sort=False).to_dict("records"))
        base_eval.copy_sample_traces(result_dir, all_trace_files)
        print(
            f"[{time.strftime('%Y-%m-%d %H:%M:%S')}] N3PF_MS_EVAL_END "
            f"new_episodes={len(all_episode_rows)} imported_episodes={len(imported_episode)} raw_steps={len(all_raw_rows)}",
            flush=True,
        )
    except N3PFMSEvalStop as exc:
        write_stop(result_dir, exc.reason, exc.detail)
        raise SystemExit(2) from exc
    except Exception:
        write_stop(result_dir, "eval_failed", traceback.format_exc())
        raise SystemExit(2)


if __name__ == "__main__":
    main()
