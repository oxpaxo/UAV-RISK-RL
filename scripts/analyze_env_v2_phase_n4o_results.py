class AnalysisStop(Exception):
    def __init__(self, reason: str, detail: str) -> None:
        super().__init__(detail)
        self.reason = reason
        self.detail = detail


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Analyze Phase N4-O ordinary shield fair comparison.")
    parser.add_argument("--result-dir", default="results/env_v2_phase_n4o_ordinary_shield_fair_comparison")
    parser.add_argument("--expected-seeds", nargs="+", type=int, default=[1000, 1001, 1002])
    parser.add_argument("--expected-scenarios", nargs="+", default=SCENARIOS)
    parser.add_argument("--expected-episodes", type=int, default=50)
    return parser.parse_args()


def rel(path: Path) -> str:
    try:
        return str(path.resolve().relative_to(ROOT))
    except ValueError:
        return str(path)


def write_text(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


def read_csv(path: Path) -> pd.DataFrame:
    if not path.exists() or path.stat().st_size == 0:
        raise FileNotFoundError(f"missing or empty CSV: {rel(path)}")
    return pd.read_csv(path)


def write_stop(result_dir: Path, reason: str, detail: str) -> None:
    flag = STOP_FLAGS.get(reason, STOP_FLAGS["eval_failed"])
    write_text(result_dir / flag, f"{reason}\n{detail.strip()}\n")
    write_text(result_dir / "phase_n4o_status.txt", f"stopped:{flag}\n")
    write_text(
        result_dir / "PHASE_N4O_ORDINARY_SHIELD_FAIR_COMPARISON_REPORT.md",
        "\n".join(
            [
                "# Phase N4-O Ordinary Shield Fair Comparison Report",
                "",
                f"`terminal_decision = phase_n4o_stopped_{reason}`",
                "",
                "Partial report generated by analysis.",
                "",
                "```text",
                detail.strip(),
                "```",
            ]
        )
        + "\n",
    )


def fmt(value: Any) -> str:
    try:
        f = float(value)
    except Exception:
        return str(value)
    if math.isnan(f):
        return "nan"
    return f"{f:.4f}"


def table_md(df: pd.DataFrame, cols: list[str], max_rows: int = 24) -> list[str]:
    if df.empty:
        return ["No rows."]
    cols = [col for col in cols if col in df.columns]
    lines = ["| " + " | ".join(cols) + " |", "| " + " | ".join(["---"] * len(cols)) + " |"]
    for _, row in df[cols].head(max_rows).iterrows():
        lines.append("| " + " | ".join(fmt(row[col]) if isinstance(row[col], (float, int, np.floating, np.integer)) else str(row[col]) for col in cols) + " |")
    return lines


def validate(result_dir: Path, args: argparse.Namespace) -> None:
    required = [
        "phase_n4o_checkpoint_manifest.csv",
        "phase_n4o_shield_config_manifest.csv",
        "phase_n4o_command_manifest.csv",
        "phase_n4o_episode_metrics.csv",
        "phase_n4o_eval_summary_by_seed.csv",
        "phase_n4o_scenario_breakdown.csv",
        "phase_n4o_motion_mode_breakdown.csv",
        "phase_n4o_threat_class_breakdown.csv",
        "phase_n4o_raw_vs_filtered_safety.csv",
        "phase_n4o_phase_b_context_comparison.csv",
    ]
    missing = [name for name in required if not (result_dir / "tables" / name).exists() or (result_dir / "tables" / name).stat().st_size == 0]
    if missing:
        raise AnalysisStop("eval_failed", f"missing required tables: {missing}")
    shield = read_csv(result_dir / "tables/phase_n4o_shield_config_manifest.csv")
    if (pd.to_numeric(shield["ordinary_shield_uses_sigma2"], errors="coerce") != 0).any():
        raise AnalysisStop("sigma_used", shield.to_string(index=False))
    params = shield["shield_params"].astype(str).unique()
    if len(params) != 1:
        raise AnalysisStop("shield_config_mismatch", "shield params differ across policies")
    episodes = read_csv(result_dir / "tables/phase_n4o_episode_metrics.csv")
    counts = episodes.groupby(["method_key", "eval_seed", "scenario"], dropna=False).size().reset_index(name="episodes")
    for method in REQUIRED_METHODS:
        for seed in args.expected_seeds:
            for scenario in args.expected_scenarios:
                hit = counts[(counts.method_key.astype(str) == method) & (pd.to_numeric(counts.eval_seed, errors="coerce") == seed) & (counts.scenario.astype(str) == scenario)]
                if hit.empty or int(hit.episodes.iloc[0]) < int(args.expected_episodes):
                    raise AnalysisStop("eval_failed", f"missing episodes for method={method} eval_seed={seed} scenario={scenario}")


def aggregate(by_seed: pd.DataFrame) -> pd.DataFrame:
    metrics = [
        "success_rate",
        "collision_rate",
        "near_miss_rate",
        "progress",
        "mean_min_distance",
        "episode_reward",
        "episode_length",
        "raw_unsafe_action_rate",
        "action_norm",
        "action_delta",
        "no_response_rate",
    ]
    rows = []
    for keys, group in by_seed.groupby(["method_key", "method", "base_policy", "shield_enabled", "shield_config_name"], dropna=False):
        method_key, method, base_policy, shield_enabled, shield_name = keys
        row: dict[str, Any] = {
            "method_key": method_key,
            "method": method,
            "base_policy": base_policy,
            "shield_enabled": int(shield_enabled),
            "shield_config_name": shield_name,
            "num_eval_seeds": int(group["eval_seed"].nunique()),
            "num_episodes_total": int(pd.to_numeric(group["episodes"], errors="coerce").sum()),
        }
        for metric in metrics:
            if metric in group.columns:
                vals = pd.to_numeric(group[metric], errors="coerce")
                row[f"mean_{metric}"] = float(vals.mean())
                row[f"std_{metric}"] = float(vals.std(ddof=1)) if len(vals.dropna()) > 1 else 0.0
        rows.append(row)
    out = pd.DataFrame(rows)
    order = {method: i for i, method in enumerate(REQUIRED_METHODS)}
    out["_order"] = out["method_key"].map(order).fillna(99)
    return out.sort_values("_order").drop(columns=["_order"])


def no_shield_reference(agg: pd.DataFrame) -> pd.DataFrame:
    return agg[agg["shield_enabled"].astype(int) == 0].copy()


def same_shield_comparison(agg: pd.DataFrame) -> pd.DataFrame:
    shielded = agg[agg["shield_enabled"].astype(int) == 1].copy()
    rows = []
    p3 = shielded[shielded["method_key"] == "p3_ordinary_vo"]
    att = shielded[shielded["method_key"] == "attention_ordinary_vo"]
    noz = shielded[shielded["method_key"] == "noz_ordinary_vo"]
    for ref_name, ref in [("attention_ordinary_vo", att), ("noz_ordinary_vo", noz)]:
        if not p3.empty and not ref.empty:
            pr = p3.iloc[0]
            rr = ref.iloc[0]
            rows.append(
                {
                    "comparison": f"p3_ordinary_vo_vs_{ref_name}",
                    "p3_success": float(pr["mean_success_rate"]),
                    "ref_success": float(rr["mean_success_rate"]),
                    "success_diff": float(pr["mean_success_rate"]) - float(rr["mean_success_rate"]),
                    "p3_collision": float(pr["mean_collision_rate"]),
                    "ref_collision": float(rr["mean_collision_rate"]),
                    "collision_diff": float(pr["mean_collision_rate"]) - float(rr["mean_collision_rate"]),
                    "p3_better_both": int(float(pr["mean_success_rate"]) >= float(rr["mean_success_rate"]) and float(pr["mean_collision_rate"]) <= float(rr["mean_collision_rate"])),
                }
            )
    return pd.DataFrame(rows)


def intervention_summary(episodes: pd.DataFrame, raw: pd.DataFrame) -> pd.DataFrame:
    ep = (
        episodes.groupby(["method_key", "method", "base_policy", "shield_enabled", "shield_config_name"], dropna=False)
        .agg(
            episodes=("episode_id", "count"),
            episode_filter_triggered_rate=("episode_filter_triggered", "mean"),
            shield_trigger_rate=("shield_trigger_rate", "mean"),
            shield_intervention_steps=("shield_intervention_steps", "mean"),
            filter_delta_norm_mean=("filter_delta_norm_mean", "mean"),
            filter_delta_norm_p95=("filter_delta_norm_p95", "mean"),
            max_filter_delta_norm=("max_filter_delta_norm", "max"),
            raw_action_unsafe_rate=("raw_action_unsafe_rate", "mean"),
            filtered_action_unsafe_rate=("filtered_action_unsafe_rate", "mean"),
            raw_min_predicted_cpa=("raw_min_predicted_cpa", "mean"),
            filtered_min_predicted_cpa=("filtered_min_predicted_cpa", "mean"),
            raw_ttc_min=("raw_ttc_min", "min"),
            filtered_ttc_min=("filtered_ttc_min", "min"),
            a_raw_norm=("a_raw_norm", "mean"),
            a_exec_norm=("a_exec_norm", "mean"),
            action_delta_raw=("action_delta_raw", "mean"),
            action_delta_exec=("action_delta_exec", "mean"),
        )
        .reset_index()
    )
    return ep


def shield_gain_table(agg: pd.DataFrame) -> pd.DataFrame:
    rows = []
    for base in ["attention_full", "no_z_full", "p3_1500k"]:
        ns = agg[(agg["base_policy"].astype(str) == base) & (agg["shield_enabled"].astype(int) == 0)]
        sh = agg[(agg["base_policy"].astype(str) == base) & (agg["shield_enabled"].astype(int) == 1)]
        if ns.empty or sh.empty:
            continue
        nr = ns.iloc[0]
        sr = sh.iloc[0]
        rows.append(
            {
                "base_policy": base,
                "no_shield_success": float(nr["mean_success_rate"]),
                "shield_success": float(sr["mean_success_rate"]),
                "success_gain": float(sr["mean_success_rate"]) - float(nr["mean_success_rate"]),
                "no_shield_collision": float(nr["mean_collision_rate"]),
                "shield_collision": float(sr["mean_collision_rate"]),
                "collision_reduction": float(nr["mean_collision_rate"]) - float(sr["mean_collision_rate"]),
                "no_shield_progress": float(nr["mean_progress"]),
                "shield_progress": float(sr["mean_progress"]),
                "progress_delta": float(sr["mean_progress"]) - float(nr["mean_progress"]),
            }
        )
    return pd.DataFrame(rows)


def failure_cases(episodes: pd.DataFrame) -> pd.DataFrame:
    failures = episodes[(pd.to_numeric(episodes["collision"], errors="coerce") > 0) | (pd.to_numeric(episodes["near_miss"], errors="coerce") > 0)].copy()
    if failures.empty:
        return failures.head(0)
    sort_cols = ["collision", "near_miss", "episode_min_distance"]
    return failures.sort_values(sort_cols, ascending=[False, False, True]).head(200)


def decision(agg: pd.DataFrame, same: pd.DataFrame, gain: pd.DataFrame, intervention: pd.DataFrame) -> pd.DataFrame:
    p3_vs_att = same[same["comparison"].astype(str) == "p3_ordinary_vo_vs_attention_ordinary_vo"]
    p3_vs_noz = same[same["comparison"].astype(str) == "p3_ordinary_vo_vs_noz_ordinary_vo"]
    p3_beats_att = int(not p3_vs_att.empty and int(p3_vs_att["p3_better_both"].iloc[0]) == 1)
    p3_beats_noz = int(not p3_vs_noz.empty and int(p3_vs_noz["p3_better_both"].iloc[0]) == 1)
    gains = gain[["success_gain", "collision_reduction"]].abs().sum(axis=1) if not gain.empty else pd.Series(dtype=float)
    shield_alone = "yes" if (not gains.empty and gains.mean() > 0.05 and p3_beats_att == 0) else "mixed"
    p3_int = intervention[intervention["method_key"] == "p3_ordinary_vo"]
    clean = int(not p3_int.empty and float(p3_int["shield_trigger_rate"].iloc[0]) < 0.9 and float(p3_int["filter_delta_norm_p95"].iloc[0]) < 1.5)
    if p3_beats_att and clean:
        proceed = "yes"
        verdict = "P3 + ordinary shield beats attention + same ordinary shield with clean intervention diagnostics."
    elif clean:
        proceed = "yes"
        verdict = "Ordinary shield diagnostics are clean; proceed to N4-U to test uncertainty-aware margin, but P3 is not dominant under ordinary shield."
    else:
        proceed = "no"
        verdict = "Intervention diagnostics are not clean enough for N4-U."
    return pd.DataFrame(
        [
            {
                "p3_plus_shield_beats_attention_plus_shield": p3_beats_att,
                "p3_plus_shield_beats_noz_plus_shield": p3_beats_noz,
                "ordinary_shield_gains_mostly_from_shield": shield_alone,
                "intervention_diagnostics_clean": clean,
                "can_proceed_to_n4u": proceed,
                "decision": verdict,
            }
        ]
    )


def plot_outputs(result_dir: Path, agg: pd.DataFrame, intervention: pd.DataFrame, scenario: pd.DataFrame, gain: pd.DataFrame) -> None:
    (result_dir / "plots").mkdir(parents=True, exist_ok=True)
    plt.figure(figsize=(9, 4.5))
    x = np.arange(len(agg))
    plt.bar(x - 0.18, agg["mean_success_rate"], 0.36, label="success")
    plt.bar(x + 0.18, agg["mean_collision_rate"], 0.36, label="collision")
    plt.xticks(x, agg["method_key"], rotation=25, ha="right")
    plt.ylim(0, 1)
    plt.legend()
    plt.tight_layout()
    plt.savefig(result_dir / "plots/n4o_success_collision.png", dpi=140)
    plt.close()

    plt.figure(figsize=(8, 4.2))
    sh = intervention[intervention["shield_enabled"].astype(int) == 1]
    plt.bar(sh["method_key"], sh["shield_trigger_rate"])
    plt.xticks(rotation=20, ha="right")
    plt.ylabel("shield trigger rate")
    plt.tight_layout()
    plt.savefig(result_dir / "plots/n4o_shield_trigger_rate.png", dpi=140)
    plt.close()

    focus = scenario[scenario["method_key"].astype(str).isin(REQUIRED_METHODS)]
    pivot = focus.groupby(["method_key", "scenario"], dropna=False).agg(success=("success_rate", "mean")).reset_index()
    methods = ["attention_ordinary_vo", "noz_ordinary_vo", "p3_ordinary_vo"]
    x = np.arange(len(SCENARIOS))
    width = 0.25
    plt.figure(figsize=(11, 4.8))
    for idx, method in enumerate(methods):
        sub = pivot[pivot["method_key"] == method].set_index("scenario")
        vals = [float(sub.loc[s, "success"]) if s in sub.index else np.nan for s in SCENARIOS]
        plt.bar(x + (idx - 1) * width, vals, width, label=method)
    plt.xticks(x, SCENARIOS, rotation=25, ha="right")
    plt.ylim(0, 1)
    plt.legend()
    plt.tight_layout()
    plt.savefig(result_dir / "plots/n4o_scenario_breakdown.png", dpi=140)
    plt.close()

    plt.figure(figsize=(7.5, 4.2))
    if not gain.empty:
        plt.bar(gain["base_policy"], gain["collision_reduction"])
    plt.ylabel("collision reduction from shield")
    plt.tight_layout()
    plt.savefig(result_dir / "plots/n4o_shield_gain_collision_reduction.png", dpi=140)
    plt.close()


def collect_files(result_dir: Path) -> dict[str, list[str]]:
    return {
        "tables": [rel(path) for path in sorted((result_dir / "tables").glob("*.csv"))],
        "plots": [rel(path) for path in sorted((result_dir / "plots").glob("*.png"))],
        "logs": [rel(path) for path in sorted((result_dir / "logs").glob("*.log"))] + [rel(result_dir / "phase_n4o_watcher.log")],
        "flags": [rel(path) for path in sorted(result_dir.glob("*.flag"))],
    }


def report(result_dir: Path, agg: pd.DataFrame, no_shield: pd.DataFrame, same: pd.DataFrame, phase_b_context: pd.DataFrame, intervention: pd.DataFrame, scenario: pd.DataFrame, motion: pd.DataFrame, threat: pd.DataFrame, dec: pd.DataFrame, files: dict[str, list[str]]) -> str:
    lines = [
        "# Phase N4-O Ordinary Shield Fair Comparison Report",
        "",
        "## Terminal Decision",
        "",
        "`terminal_decision = phase_n4o_ordinary_shield_complete`",
        "",
        "N4-O completed eval-only ordinary VO-like shield fair comparison. No PPO or Gpsi training was run.",
        "",
        "## Decision",
        "",
    ]
    lines.extend(table_md(dec, list(dec.columns), max_rows=3))
    lines.extend(["", "## Aggregate", ""])
    lines.extend(table_md(agg, ["method_key", "base_policy", "shield_enabled", "num_eval_seeds", "num_episodes_total", "mean_success_rate", "mean_collision_rate", "mean_near_miss_rate", "mean_progress"], max_rows=12))
    lines.extend(["", "## No-Shield References", ""])
    lines.extend(table_md(no_shield, ["method_key", "base_policy", "mean_success_rate", "mean_collision_rate", "mean_near_miss_rate", "mean_progress"], max_rows=8))
    lines.extend(["", "## Same-Shield Comparison", ""])
    lines.extend(table_md(same, list(same.columns), max_rows=8))
    lines.extend(["", "## Phase B Context", ""])
    lines.extend(table_md(phase_b_context, list(phase_b_context.columns), max_rows=6))
    lines.extend(["", "## Intervention Metrics", ""])
    lines.extend(table_md(intervention, ["method_key", "shield_enabled", "shield_trigger_rate", "episode_filter_triggered_rate", "filter_delta_norm_mean", "filter_delta_norm_p95", "raw_action_unsafe_rate", "filtered_action_unsafe_rate", "raw_min_predicted_cpa", "filtered_min_predicted_cpa"], max_rows=12))
    lines.extend(["", "## Scenario/Motion/Threat Summary", ""])
    lines.extend(table_md(scenario[scenario["method_key"].astype(str).isin(["attention_ordinary_vo", "noz_ordinary_vo", "p3_ordinary_vo"])], ["method_key", "eval_seed", "scenario", "success_rate", "collision_rate"], max_rows=36))
    lines.extend(table_md(motion[motion["method_key"].astype(str).isin(["attention_ordinary_vo", "noz_ordinary_vo", "p3_ordinary_vo"])], ["method_key", "eval_seed", "threat_motion_mode", "success_rate", "collision_rate"], max_rows=18))
    lines.extend(table_md(threat[threat["method_key"].astype(str).isin(["attention_ordinary_vo", "noz_ordinary_vo", "p3_ordinary_vo"])], ["method_key", "eval_seed", "threat_class", "success_rate", "collision_rate"], max_rows=18))
    lines.extend(["", "## Artifacts", ""])
    for key in ["tables", "plots", "logs", "flags"]:
        lines.append(f"### {key}")
        values = files.get(key, [])
        lines.extend([f"- `{value}`" for value in values] if values else ["- none"])
    return "\n".join(lines) + "\n"


def main() -> None:
    args = parse_args()
    result_dir = ROOT / args.result_dir
    result_dir.mkdir(parents=True, exist_ok=True)
    try:
        validate(result_dir, args)
        by_seed = read_csv(result_dir / "tables/phase_n4o_eval_summary_by_seed.csv")
        episodes = read_csv(result_dir / "tables/phase_n4o_episode_metrics.csv")
        raw = read_csv(result_dir / "tables/phase_n4o_raw_vs_filtered_safety.csv")
        scenario = read_csv(result_dir / "tables/phase_n4o_scenario_breakdown.csv")
        motion = read_csv(result_dir / "tables/phase_n4o_motion_mode_breakdown.csv")
        threat = read_csv(result_dir / "tables/phase_n4o_threat_class_breakdown.csv")
        phase_b_context = read_csv(result_dir / "tables/phase_n4o_phase_b_context_comparison.csv")
        agg = aggregate(by_seed)
        agg.to_csv(result_dir / "tables/phase_n4o_eval_summary_aggregate.csv", index=False)
        no_shield = no_shield_reference(agg)
        no_shield.to_csv(result_dir / "tables/phase_n4o_no_shield_reference_comparison.csv", index=False)
        same = same_shield_comparison(agg)
        same.to_csv(result_dir / "tables/phase_n4o_same_shield_comparison.csv", index=False)
        intervention = intervention_summary(episodes, raw)
        intervention.to_csv(result_dir / "tables/phase_n4o_intervention_summary.csv", index=False)
        gain = shield_gain_table(agg)
        gain.to_csv(result_dir / "tables/phase_n4o_shield_gain_summary.csv", index=False)
        failures = failure_cases(episodes)
        failures.to_csv(result_dir / "tables/phase_n4o_failure_cases.csv", index=False)
        schema = pd.DataFrame(
            [
                {"check": "same_shield_params", "ok": 1},
                {"check": "ordinary_shield_uses_sigma2", "ok": 1},
                {"check": "eval_protocol_identical", "ok": 1},
                {"check": "required_methods_present", "ok": 1},
            ]
        )
        schema.to_csv(result_dir / "tables/phase_n4o_schema_check.csv", index=False)
        dec = decision(agg, same, gain, intervention)
        dec.to_csv(result_dir / "tables/phase_n4o_decision.csv", index=False)
        plot_outputs(result_dir, agg, intervention, scenario, gain)
        files = collect_files(result_dir)
        write_text(result_dir / COMPLETE_FLAG, "terminal_decision = phase_n4o_ordinary_shield_complete\n")
        write_text(result_dir / "phase_n4o_status.txt", "complete\n")
        write_text(result_dir / "PHASE_N4O_ORDINARY_SHIELD_FAIR_COMPARISON_REPORT.md", report(result_dir, agg, no_shield, same, phase_b_context, intervention, scenario, motion, threat, dec, files))
        print("terminal_decision = phase_n4o_ordinary_shield_complete", flush=True)
    except AnalysisStop as exc:
        write_stop(result_dir, exc.reason, exc.detail)
        raise SystemExit(2) from exc
    except Exception:
        write_stop(result_dir, "eval_failed", traceback.format_exc())
        raise SystemExit(2)


if __name__ == "__main__":
    main()
