ROOT = Path(__file__).resolve().parents[1]
if str(ROOT) not in sys.path:
    sys.path.insert(0, str(ROOT))


COMPLETE_FLAG = "PHASE_N3PF_MS_MULTI_SEED_COMPLETE.flag"
STOP_FLAGS = {
    "config_mismatch": "PHASE_N3PF_MS_STOP_CONFIG_MISMATCH.flag",
    "train_failed": "PHASE_N3PF_MS_STOP_TRAIN_FAILED.flag",
    "eval_failed": "PHASE_N3PF_MS_STOP_EVAL_FAILED.flag",
    "diagnostics_failed": "PHASE_N3PF_MS_STOP_DIAGNOSTICS_FAILED.flag",
    "watcher_failed": "PHASE_N3PF_MS_STOP_WATCHER_FAILED.flag",
}

SCENARIOS = [
    "eval_flow_id",
    "eval_flow_high_density",
    "eval_flow_high_speed",
    "eval_flow_high_threat",
    "eval_flow_mixed_ood",
    "eval_flow_sudden_threat",
]


class AnalysisStop(Exception):
    def __init__(self, reason: str, detail: str) -> None:
        super().__init__(detail)
        self.reason = reason
        self.detail = detail


def parse_args() -> argparse.Namespace:
    parser = argparse.ArgumentParser(description="Analyze Phase N3PF-MS multi-training-seed results.")
    parser.add_argument("--result-dir", default="results/env_v2_phase_n3pf_ms_multiseed")
    parser.add_argument("--expected-training-seeds", nargs="+", type=int, default=[1, 2])
    parser.add_argument("--expected-eval-seeds", nargs="+", type=int, default=[1000, 1001, 1002])
    parser.add_argument("--expected-scenarios", nargs="+", default=SCENARIOS)
    parser.add_argument("--expected-episodes", type=int, default=50)
    parser.add_argument("--attention-success", type=float, default=0.6033)
    parser.add_argument("--attention-collision", type=float, default=0.3967)
    parser.add_argument("--noz-success", type=float, default=0.5667)
    parser.add_argument("--noz-collision", type=float, default=0.4333)
    return parser.parse_args()


def rel(path: Path) -> str:
    try:
        return str(path.resolve().relative_to(ROOT))
    except ValueError:
        return str(path)


def write_text(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


def read_csv(path: Path) -> pd.DataFrame:
    if not path.exists() or path.stat().st_size == 0:
        raise FileNotFoundError(f"missing or empty CSV: {rel(path)}")
    return pd.read_csv(path)


def write_stop(result_dir: Path, reason: str, detail: str) -> None:
    flag = STOP_FLAGS.get(reason, STOP_FLAGS["diagnostics_failed"])
    write_text(result_dir / flag, f"{reason}\n{detail.strip()}\n")
    write_text(result_dir / "phase_n3pf_ms_status.txt", f"stopped:{flag}\n")
    write_text(
        result_dir / "PHASE_N3PF_MS_MULTI_SEED_REPORT.md",
        "\n".join(
            [
                "# Phase N3PF-MS Multi-Training-Seed Report",
                "",
                f"`terminal_decision = phase_n3pf_ms_stopped_{reason}`",
                "",
                "Partial report generated by analysis.",
                "",
                "```text",
                detail.strip(),
                "```",
            ]
        )
        + "\n",
    )


def fmt(value: Any) -> str:
    try:
        f = float(value)
    except Exception:
        return str(value)
    if math.isnan(f):
        return "nan"
    return f"{f:.4f}"


def table_md(df: pd.DataFrame, cols: list[str], max_rows: int = 24) -> list[str]:
    if df.empty:
        return ["No rows."]
    cols = [col for col in cols if col in df.columns]
    lines = ["| " + " | ".join(cols) + " |", "| " + " | ".join(["---"] * len(cols)) + " |"]
    for _, row in df[cols].head(max_rows).iterrows():
        lines.append("| " + " | ".join(fmt(row[col]) if isinstance(row[col], (float, int, np.floating, np.integer)) else str(row[col]) for col in cols) + " |")
    return lines


def validate(result_dir: Path, args: argparse.Namespace) -> None:
    required = [
        "phase_n3pf_ms_config_manifest.csv",
        "phase_n3pf_ms_command_manifest.csv",
        "phase_n3pf_ms_resource_affinity.csv",
        "phase_n3pf_ms_train_heartbeat.csv",
        "phase_n3pf_ms_train_curve.csv",
        "phase_n3pf_ms_checkpoint_integrity.csv",
        "phase_n3pf_ms_eval_summary_by_seed.csv",
        "phase_n3pf_ms_scenario_breakdown.csv",
        "phase_n3pf_ms_motion_mode_breakdown.csv",
        "phase_n3pf_ms_threat_class_breakdown.csv",
        "phase_n3pf_ms_raw_unsafe_action_summary.csv",
        "phase_n3pf_ms_feature_block_stats.csv",
        "phase_n3pf_ms_gpsi_output_summary.csv",
        "phase_n3pf_ms_schema_check.csv",
    ]
    missing = [name for name in required if not (result_dir / "tables" / name).exists() or (result_dir / "tables" / name).stat().st_size == 0]
    if missing:
        raise AnalysisStop("eval_failed", f"missing required tables: {missing}")
    integrity = read_csv(result_dir / "tables/phase_n3pf_ms_checkpoint_integrity.csv")
    bad_ckpt = integrity[integrity["exists"].astype(int) != 1]
    if not bad_ckpt.empty:
        raise AnalysisStop("train_failed", bad_ckpt.to_string(index=False))
    episodes = read_csv(result_dir / "tables/phase_n3pf_ms_episode_metrics.csv")
    for train_seed in args.expected_training_seeds:
        for label in ["1000k", "1500k", "final"]:
            key = f"p3_s{train_seed}_{label}"
            subset = episodes[episodes["method_key"].astype(str) == key]
            counts = subset.groupby(["eval_seed", "scenario"], dropna=False).size().reset_index(name="episodes")
            for eval_seed in args.expected_eval_seeds:
                for scenario in args.expected_scenarios:
                    hit = counts[(pd.to_numeric(counts.eval_seed, errors="coerce") == eval_seed) & (counts.scenario.astype(str) == scenario)]
                    if hit.empty or int(hit.episodes.iloc[0]) < int(args.expected_episodes):
                        raise AnalysisStop("eval_failed", f"missing episodes for {key} eval_seed={eval_seed} scenario={scenario}")


def aggregate(by_seed: pd.DataFrame) -> pd.DataFrame:
    metrics = [
        "success_rate",
        "collision_rate",
        "near_miss_rate",
        "progress",
        "mean_min_distance",
        "episode_reward",
        "episode_length",
        "raw_unsafe_action_rate",
        "action_norm",
        "action_delta",
        "no_response_rate",
    ]
    rows: list[dict[str, Any]] = []
    for keys, group in by_seed.groupby(["training_seed", "method_key", "checkpoint_label"], dropna=False):
        train_seed, method_key, label = keys
        row: dict[str, Any] = {
            "training_seed": train_seed,
            "method_key": method_key,
            "checkpoint_label": label,
            "num_eval_seeds": int(group["eval_seed"].nunique()) if "eval_seed" in group.columns else 0,
            "num_episodes_total": int(pd.to_numeric(group.get("episodes", pd.Series(dtype=float)), errors="coerce").sum()),
        }
        for metric in metrics:
            if metric in group.columns:
                values = pd.to_numeric(group[metric], errors="coerce")
                row[f"mean_{metric}"] = float(values.mean())
                row[f"std_{metric}"] = float(values.std(ddof=1)) if len(values.dropna()) > 1 else 0.0
        rows.append(row)
    out = pd.DataFrame(rows)
    if out.empty:
        return out
    order = {"1000k": 0, "1500k": 1, "final": 2}
    out["_order"] = out["checkpoint_label"].map(order).fillna(9)
    return out.sort_values(["training_seed", "_order", "method_key"]).drop(columns=["_order"])


def training_seed_summary(agg: pd.DataFrame) -> pd.DataFrame:
    p3 = agg[agg["method_key"].astype(str).str.match(r"p3_s[012]_(1000k|1500k|final)$")].copy()
    rows: list[dict[str, Any]] = []
    for label, group in p3.groupby("checkpoint_label", dropna=False):
        rows.append(
            {
                "checkpoint_label": label,
                "training_seed_count": int(group["training_seed"].nunique()),
                "mean_success_rate": float(pd.to_numeric(group["mean_success_rate"], errors="coerce").mean()),
                "std_success_rate_across_training_seeds": float(pd.to_numeric(group["mean_success_rate"], errors="coerce").std(ddof=1)),
                "min_success_rate": float(pd.to_numeric(group["mean_success_rate"], errors="coerce").min()),
                "mean_collision_rate": float(pd.to_numeric(group["mean_collision_rate"], errors="coerce").mean()),
                "std_collision_rate_across_training_seeds": float(pd.to_numeric(group["mean_collision_rate"], errors="coerce").std(ddof=1)),
                "max_collision_rate": float(pd.to_numeric(group["mean_collision_rate"], errors="coerce").max()),
                "collapse_warning_count": int(((pd.to_numeric(group["mean_success_rate"], errors="coerce") < 0.55) | (pd.to_numeric(group["mean_collision_rate"], errors="coerce") > 0.45)).sum()),
            }
        )
    return pd.DataFrame(rows)


def pairwise_vs_attention(agg: pd.DataFrame) -> pd.DataFrame:
    att = agg[agg["method_key"].astype(str) == "attention_full"]
    if att.empty:
        raise AnalysisStop("eval_failed", "missing imported attention_full reference")
    att_success = float(att["mean_success_rate"].iloc[0])
    att_collision = float(att["mean_collision_rate"].iloc[0])
    rows = []
    for _, row in agg[agg["method_key"].astype(str).str.startswith("p3_s")].iterrows():
        rows.append(
            {
                "training_seed": row["training_seed"],
                "method_key": row["method_key"],
                "checkpoint_label": row["checkpoint_label"],
                "success_rate": float(row["mean_success_rate"]),
                "collision_rate": float(row["mean_collision_rate"]),
                "attention_success": att_success,
                "attention_collision": att_collision,
                "success_diff_vs_attention": float(row["mean_success_rate"]) - att_success,
                "collision_diff_vs_attention": float(row["mean_collision_rate"]) - att_collision,
                "better_both": int(float(row["mean_success_rate"]) >= att_success and float(row["mean_collision_rate"]) <= att_collision),
            }
        )
    return pd.DataFrame(rows)


def diagnostics(result_dir: Path) -> pd.DataFrame:
    feats = read_csv(result_dir / "tables/phase_n3pf_ms_feature_block_stats.csv")
    gpsi = read_csv(result_dir / "tables/phase_n3pf_ms_gpsi_output_summary.csv")
    p3_feats = feats[feats["method_key"].astype(str).str.startswith("p3_s")]
    p3_gpsi = gpsi[gpsi["method_key"].astype(str).str.startswith("p3_s")]
    if p3_feats.empty or p3_gpsi.empty:
        raise AnalysisStop("diagnostics_failed", "missing P3 diagnostics")
    nonfinite = int(pd.to_numeric(p3_feats.get("nan_count", pd.Series(dtype=float)), errors="coerce").fillna(0).sum())
    nonfinite += int(pd.to_numeric(p3_feats.get("inf_count", pd.Series(dtype=float)), errors="coerce").fillna(0).sum())
    delta_p95 = pd.to_numeric(p3_gpsi.get("delta_norm_1s_p95", pd.Series(dtype=float)), errors="coerce").max()
    delta_max = pd.to_numeric(p3_gpsi.get("delta_norm_1s_max", pd.Series(dtype=float)), errors="coerce").max()
    adapter = p3_feats[p3_feats["block"].astype(str) == "adapter_output_64"]
    adapter_l2 = pd.to_numeric(adapter.get("l2_norm_p95", pd.Series(dtype=float)), errors="coerce").max()
    ok = np.isfinite(delta_p95) and delta_p95 < 100.0 and np.isfinite(delta_max) and delta_max < 1000.0 and np.isfinite(adapter_l2) and 1.0 <= adapter_l2 <= 12.0 and nonfinite == 0
    out = pd.DataFrame(
        [
            {
                "diagnostics_ok": int(ok),
                "delta_norm_1s_p95_max": float(delta_p95),
                "delta_norm_1s_max": float(delta_max),
                "adapter_output_l2_p95_max": float(adapter_l2),
                "feature_nonfinite_count": int(nonfinite),
            }
        ]
    )
    out.to_csv(result_dir / "tables/phase_n3pf_ms_diagnostics_decision.csv", index=False)
    if not ok:
        raise AnalysisStop("diagnostics_failed", out.to_string(index=False))
    return out


def decision(summary: pd.DataFrame, diag: pd.DataFrame, args: argparse.Namespace) -> pd.DataFrame:
    row1500 = summary[summary["checkpoint_label"].astype(str) == "1500k"]
    if row1500.empty:
        raise AnalysisStop("eval_failed", "missing 1500k training-seed summary")
    r = row1500.iloc[0]
    mean_success = float(r["mean_success_rate"])
    mean_collision = float(r["mean_collision_rate"])
    stable = int(diag["diagnostics_ok"].iloc[0]) == 1 and int(r["collapse_warning_count"]) == 0 and mean_success >= args.noz_success - 0.02 and mean_collision <= args.noz_collision + 0.02
    strong_positive = mean_success >= args.attention_success + 0.02 and mean_collision <= args.attention_collision - 0.02
    comparable_positive = mean_success >= args.attention_success - 0.01 and mean_collision <= args.attention_collision + 0.01
    if stable and strong_positive:
        status = "strong_positive"
        n4 = "yes"
        attention_ms = "recommended_for_claim_strength"
    elif stable and comparable_positive:
        status = "comparable_positive"
        n4 = "yes"
        attention_ms = "recommended_due_small_margin"
    elif stable:
        status = "stable_but_below_attention"
        n4 = "yes"
        attention_ms = "not_priority_until_p3_gap_clarified"
    else:
        status = "unstable_or_collapsed"
        n4 = "no"
        attention_ms = "no"
    return pd.DataFrame(
        [
            {
                "p3_multiseed_status": status,
                "p3_1500k_training_seed_mean_success": mean_success,
                "p3_1500k_training_seed_mean_collision": mean_collision,
                "attention_success_reference": float(args.attention_success),
                "attention_collision_reference": float(args.attention_collision),
                "noz_success_reference": float(args.noz_success),
                "noz_collision_reference": float(args.noz_collision),
                "stable_minimum_gate": int(stable),
                "strong_positive_gate": int(strong_positive),
                "comparable_positive_gate": int(comparable_positive),
                "attention_full_multiseed_needed": attention_ms,
                "n4_can_proceed": n4,
                "diagnostics_ok": int(diag["diagnostics_ok"].iloc[0]),
            }
        ]
    )


def plot_outputs(result_dir: Path, agg: pd.DataFrame, summary: pd.DataFrame, scenario: pd.DataFrame) -> None:
    (result_dir / "plots").mkdir(parents=True, exist_ok=True)
    p3 = agg[agg["method_key"].astype(str).str.startswith("p3_s")]
    plt.figure(figsize=(9, 4.5))
    for seed, group in p3.groupby("training_seed"):
        group = group[group["checkpoint_label"].astype(str).isin(["1000k", "1500k", "final"])]
        order = {"1000k": 0, "1500k": 1, "final": 2}
        group = group.assign(_order=group["checkpoint_label"].map(order)).sort_values("_order")
        plt.plot(group["checkpoint_label"], group["mean_success_rate"], marker="o", label=f"seed{seed} success")
        plt.plot(group["checkpoint_label"], group["mean_collision_rate"], marker="x", linestyle="--", label=f"seed{seed} collision")
    plt.ylim(0, 1)
    plt.legend(fontsize=8)
    plt.tight_layout()
    plt.savefig(result_dir / "plots/n3pf_ms_checkpoint_success_collision.png", dpi=140)
    plt.close()

    plt.figure(figsize=(7.5, 4.2))
    plt.bar(summary["checkpoint_label"], summary["mean_success_rate"], yerr=summary["std_success_rate_across_training_seeds"], capsize=3, label="success")
    plt.plot(summary["checkpoint_label"], summary["mean_collision_rate"], marker="o", color="tab:red", label="collision")
    plt.ylim(0, 1)
    plt.legend()
    plt.tight_layout()
    plt.savefig(result_dir / "plots/n3pf_ms_training_seed_stability.png", dpi=140)
    plt.close()

    focus = scenario[(scenario["method_key"].astype(str).str.contains("_1500k")) & (scenario["training_seed"].isin([0, 1, 2]))]
    pivot = focus.groupby(["training_seed", "scenario"], dropna=False).agg(success=("success_rate", "mean")).reset_index()
    plt.figure(figsize=(11, 4.8))
    x = np.arange(len(SCENARIOS))
    width = 0.24
    for idx, seed in enumerate([0, 1, 2]):
        vals = []
        sub = pivot[pivot["training_seed"] == seed].set_index("scenario")
        for scenario_name in SCENARIOS:
            vals.append(float(sub.loc[scenario_name, "success"]) if scenario_name in sub.index else np.nan)
        plt.bar(x + (idx - 1) * width, vals, width, label=f"seed{seed}")
    plt.xticks(x, SCENARIOS, rotation=25, ha="right")
    plt.ylim(0, 1)
    plt.legend()
    plt.tight_layout()
    plt.savefig(result_dir / "plots/n3pf_ms_scenario_breakdown.png", dpi=140)
    plt.close()


def collect_files(result_dir: Path) -> dict[str, list[str]]:
    return {
        "tables": [rel(path) for path in sorted((result_dir / "tables").glob("*.csv"))],
        "plots": [rel(path) for path in sorted((result_dir / "plots").glob("*.png"))],
        "logs": [rel(path) for path in sorted((result_dir / "logs").glob("*.log"))] + [rel(result_dir / "phase_n3pf_ms_watcher.log")],
        "flags": [rel(path) for path in sorted(result_dir.glob("*.flag"))],
    }


def report(result_dir: Path, agg: pd.DataFrame, summary: pd.DataFrame, pairwise: pd.DataFrame, dec: pd.DataFrame, diag: pd.DataFrame, files: dict[str, list[str]]) -> str:
    scenario = read_csv(result_dir / "tables/phase_n3pf_ms_scenario_breakdown.csv")
    motion = read_csv(result_dir / "tables/phase_n3pf_ms_motion_mode_breakdown.csv")
    threat = read_csv(result_dir / "tables/phase_n3pf_ms_threat_class_breakdown.csv")
    lines = [
        "# Phase N3PF-MS Multi-Training-Seed Report",
        "",
        "## Terminal Decision",
        "",
        "`terminal_decision = phase_n3pf_ms_multi_seed_complete`",
        "",
        "P3 block_projected training seeds 1 and 2 completed and were evaluated with the imported seed0/reference verification rows.",
        "",
        "## Decision",
        "",
    ]
    lines.extend(table_md(dec, list(dec.columns), max_rows=3))
    lines.extend(["", "## Training-Seed Summary", ""])
    lines.extend(table_md(summary, ["checkpoint_label", "training_seed_count", "mean_success_rate", "std_success_rate_across_training_seeds", "min_success_rate", "mean_collision_rate", "std_collision_rate_across_training_seeds", "max_collision_rate", "collapse_warning_count"], max_rows=10))
    lines.extend(["", "## Aggregate", ""])
    lines.extend(table_md(agg, ["training_seed", "method_key", "checkpoint_label", "num_eval_seeds", "num_episodes_total", "mean_success_rate", "mean_collision_rate", "mean_near_miss_rate", "mean_raw_unsafe_action_rate"], max_rows=24))
    lines.extend(["", "## Pairwise Vs Attention", ""])
    lines.extend(table_md(pairwise, ["training_seed", "method_key", "checkpoint_label", "success_diff_vs_attention", "collision_diff_vs_attention", "better_both"], max_rows=24))
    lines.extend(["", "## Scenario/Motion/Threat Summary", ""])
    lines.extend(table_md(scenario[scenario["method_key"].astype(str).str.contains("_1500k")], ["training_seed", "method_key", "eval_seed", "scenario", "success_rate", "collision_rate"], max_rows=36))
    lines.extend(table_md(motion[motion["method_key"].astype(str).str.contains("_1500k")], ["training_seed", "method_key", "eval_seed", "threat_motion_mode", "success_rate", "collision_rate"], max_rows=18))
    lines.extend(table_md(threat[threat["method_key"].astype(str).str.contains("_1500k")], ["training_seed", "method_key", "eval_seed", "threat_class", "success_rate", "collision_rate"], max_rows=18))
    lines.extend(["", "## Diagnostics", ""])
    lines.extend(table_md(diag, list(diag.columns), max_rows=5))
    lines.extend(["", "## Artifacts", ""])
    for key in ["tables", "plots", "logs", "flags"]:
        lines.append(f"### {key}")
        values = files.get(key, [])
        lines.extend([f"- `{value}`" for value in values] if values else ["- none"])
    return "\n".join(lines) + "\n"


def main() -> None:
    args = parse_args()
    result_dir = ROOT / args.result_dir
    result_dir.mkdir(parents=True, exist_ok=True)
    try:
        validate(result_dir, args)
        by_seed = read_csv(result_dir / "tables/phase_n3pf_ms_eval_summary_by_seed.csv")
        scenario = read_csv(result_dir / "tables/phase_n3pf_ms_scenario_breakdown.csv")
        agg = aggregate(by_seed)
        agg.to_csv(result_dir / "tables/phase_n3pf_ms_eval_summary_aggregate.csv", index=False)
        seed_summary = training_seed_summary(agg)
        seed_summary.to_csv(result_dir / "tables/phase_n3pf_ms_training_seed_summary.csv", index=False)
        pairwise = pairwise_vs_attention(agg)
        pairwise.to_csv(result_dir / "tables/phase_n3pf_ms_pairwise_vs_attention.csv", index=False)
        diag = diagnostics(result_dir)
        dec = decision(seed_summary, diag, args)
        dec.to_csv(result_dir / "tables/phase_n3pf_ms_decision.csv", index=False)
        plot_outputs(result_dir, agg, seed_summary, scenario)
        files = collect_files(result_dir)
        write_text(result_dir / COMPLETE_FLAG, "terminal_decision = phase_n3pf_ms_multi_seed_complete\n")
        write_text(result_dir / "phase_n3pf_ms_status.txt", "complete\n")
        write_text(result_dir / "PHASE_N3PF_MS_MULTI_SEED_REPORT.md", report(result_dir, agg, seed_summary, pairwise, dec, diag, files))
        print("terminal_decision = phase_n3pf_ms_multi_seed_complete", flush=True)
    except AnalysisStop as exc:
        write_stop(result_dir, exc.reason, exc.detail)
        raise SystemExit(2) from exc
    except Exception:
        write_stop(result_dir, "diagnostics_failed", traceback.format_exc())
        raise SystemExit(2)


if __name__ == "__main__":
    main()
