RESULT_DIR = ROOT / "results/env_v2_phase_n3pf_ms_multiseed"
N3PFV_FLAG = ROOT / "results/env_v2_phase_n3pfv_checkpoint_verification/PHASE_N3PFV_CHECKPOINT_VERIFICATION_COMPLETE.flag"
