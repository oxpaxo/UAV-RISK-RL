def write_checkpoint_manifest(result_dir: Path, specs: dict[str, dict[str, Any]]) -> None:
    rows = []
    for key, spec in specs.items():
        path = ROOT / str(spec["path"])
        exists = path.exists() and path.stat().st_size > 0
        rows.append(
            {
                "method_key": key,
                "base_policy": spec["base_policy"],
                "method": spec["method"],
                "checkpoint_label": spec["checkpoint_label"],
                "checkpoint_path": rel(path),
                "exists": int(exists),
                "size_bytes": int(path.stat().st_size) if exists else 0,
                "sha256": sha256(path) if exists else "missing",
                "config": spec["cfg"] or "",
                "shield_enabled": int(spec["shield_enabled"]),
                "source_phase": spec["source_phase"],
            }
        )
    write_csv(result_dir / "tables/phase_n4o_checkpoint_manifest.csv", rows)
    missing = [row for row in rows if int(row["exists"]) != 1]
    if missing:
        raise N4OEvalStop("checkpoint_missing", pd.DataFrame(missing).to_string(index=False))


