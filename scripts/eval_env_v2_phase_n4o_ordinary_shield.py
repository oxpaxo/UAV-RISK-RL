def rel(path: Path) -> str:
    try:
        return str(path.resolve().relative_to(ROOT))
    except ValueError:
        return str(path)


def ensure_dirs(result_dir: Path) -> None:
    for path in [result_dir, result_dir / "logs", result_dir / "tables", result_dir / "plots", result_dir / "traces"]:
        path.mkdir(parents=True, exist_ok=True)


def write_text(path: Path, text: str) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text, encoding="utf-8")


def write_csv(path: Path, rows: list[dict[str, Any]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    if not rows:
        path.write_text("", encoding="utf-8")
        return
    fields: list[str] = []
    seen: set[str] = set()
    for row in rows:
        for key in row:
            if key not in seen:
                fields.append(key)
                seen.add(key)
    with path.open("w", newline="", encoding="utf-8") as handle:
        writer = csv.DictWriter(handle, fieldnames=fields, extrasaction="ignore")
        writer.writeheader()
        writer.writerows(rows)


def append_csv(path: Path, rows: list[dict[str, Any]]) -> None:
    old: list[dict[str, Any]] = []
    if path.exists() and path.stat().st_size > 0:
        with path.open("r", newline="", encoding="utf-8") as handle:
            old = list(csv.DictReader(handle))
    write_csv(path, old + rows)


def write_stop(result_dir: Path, reason: str, detail: str) -> None:
    ensure_dirs(result_dir)
    flag = STOP_FLAGS.get(reason, STOP_FLAGS["eval_failed"])
    write_text(result_dir / flag, f"{reason}\n{detail.strip()}\n")
    write_text(result_dir / "phase_n4o_status.txt", f"stopped:{flag}\n")
    write_text(
        result_dir / "PHASE_N4O_ORDINARY_SHIELD_FAIR_COMPARISON_REPORT.md",
        "\n".join(
            [
                "# Phase N4-O Ordinary Shield Fair Comparison Report",
                "",
                f"`terminal_decision = phase_n4o_stopped_{reason}`",
                "",
                "Partial report generated by eval script.",
                "",
                "```text",
                detail.strip(),
                "```",
            ]
        )
        + "\n",
    )


def sha256(path: Path) -> str:
    digest = hashlib.sha256()
    with path.open("rb") as handle:
        for chunk in iter(lambda: handle.read(1024 * 1024), b""):
            digest.update(chunk)
    return digest.hexdigest()


def load_yaml(path: Path) -> dict[str, Any]:
    with path.open("r", encoding="utf-8") as handle:
        payload = yaml.safe_load(handle)
    if not isinstance(payload, dict):
        raise N4OEvalStop("schema_invalid", f"config is not a mapping: {rel(path)}")
    return payload


def load_phase_b_shield(args: argparse.Namespace) -> phase_b.BaselineConfig:
    manifest_path = ROOT / args.phase_b_dir / "tables/phase_b_baseline_manifest.csv"
    if not manifest_path.exists():
        raise N4OEvalStop("phase_b_config_missing", f"missing Phase B manifest: {rel(manifest_path)}")
    manifest = pd.read_csv(manifest_path)
    row = manifest[manifest["config_name"].astype(str) == str(args.shield_config_name)]
    if row.empty:
        raise N4OEvalStop("phase_b_config_missing", f"shield config not found in Phase B manifest: {args.shield_config_name}")
    r = row.iloc[0]
    params = json.loads(str(r["config_params"]))
    if str(r["baseline_name"]) != "vo_like_filter" or str(r["kind"]) != "vo_like_filter" or int(r["filter_used"]) != 1:
        raise N4OEvalStop("shield_config_mismatch", row.to_string(index=False))
    required = {"horizon": 4.5, "cpa_safe": 1.2, "num_headings": 16}
    for key, expected in required.items():
        if key not in params or abs(float(params[key]) - float(expected)) > 1e-9:
            raise N4OEvalStop("shield_config_mismatch", f"unexpected {key}: {params}")
    return phase_b.BaselineConfig(
        baseline_name=str(r["baseline_name"]),
        config_name=str(r["config_name"]),
        category=str(r["baseline_category"]),
        kind=str(r["kind"]),
        params=params,
    )


