def evaluate_episode(
    *,
    model: PPO,
    env: DynamicObstacleFlowEnv | GpsiObsWrapper,
    cfg: dict[str, Any] | None,
    method_key: str,
    spec: dict[str, Any],
    shield: phase_b.BaselineConfig,
    scenario: str,
    eval_seed: int,
    episode_id: int,
    episode_seed: int,
    args: argparse.Namespace,
    runtime_args: SimpleNamespace,
    gpsi_accum: dict[tuple[Any, ...], dict[str, base_eval.ScalarAccumulator]],
    feature_accum: dict[tuple[Any, ...], dict[str, base_eval.BlockAccumulator]],
) -> tuple[dict[str, Any], list[dict[str, Any]]]:
    obs, info = env.reset(seed=episode_seed)
    done = False
    steps = 0
    episode_return = 0.0
    last_info = info
    min_distances: list[float] = []
    raw_cpas: list[float] = []
    filtered_cpas: list[float] = []
    raw_ttcs: list[float] = []
    filtered_ttcs: list[float] = []
    action_norm_raw: list[float] = []
    action_norm_exec: list[float] = []
    action_delta_raw: list[float] = []
    action_delta_exec: list[float] = []
    filter_deltas: list[float] = []
    filter_triggers = 0
    filter_triggered_any = False
    prev_raw: np.ndarray | None = None
    prev_exec: np.ndarray | None = None
    raw_rows: list[dict[str, Any]] = []
    prefix_base = {
        "method_key": method_key,
        "method": spec["method"],
        "base_policy": spec["base_policy"],
        "checkpoint": spec["method"],
        "checkpoint_path": rel(ROOT / str(spec["path"])),
        "checkpoint_step": 1_500_000,
        "checkpoint_label": spec["checkpoint_label"],
        "shield_enabled": int(spec["shield_enabled"]),
        "shield_config_name": shield.config_name if int(spec["shield_enabled"]) else "none",
        "scenario": scenario,
        "eval_seed": int(eval_seed),
    }
    while not done:
        raw_action, _ = model.predict(obs, deterministic=True)
        raw_action = np.clip(np.asarray(raw_action, dtype=np.float32), -1.0, 1.0)
        fenv = flow_env(env)
        if int(spec["shield_enabled"]):
            action_exec, triggered, reason, debug = phase_b.vo_like_filter(raw_action, fenv, info, shield)
        else:
            action_exec = raw_action.copy()
            triggered = False
            reason = "none"
            debug = phase_b.filter_debug(raw_action, action_exec, fenv, info, horizon=float(shield.params["horizon"]))
        action_exec = np.clip(np.asarray(action_exec, dtype=np.float32), -1.0, 1.0)
        filter_delta = float(np.linalg.norm(action_exec - raw_action))
        filter_deltas.append(filter_delta)
        filter_triggers += int(triggered)
        filter_triggered_any = filter_triggered_any or bool(triggered)
        raw_cpas.append(phase_b.finite(debug.get("min_predicted_cpa_raw")))
        filtered_cpas.append(phase_b.finite(debug.get("min_predicted_cpa_filtered")))
        raw_ttcs.append(phase_b.finite(debug.get("min_ttc_raw")))
        filtered_ttcs.append(phase_b.finite(debug.get("min_ttc_filtered")))
        action_norm_raw.append(float(np.linalg.norm(raw_action)))
        action_norm_exec.append(float(np.linalg.norm(action_exec)))
        if prev_raw is not None:
            action_delta_raw.append(float(np.linalg.norm(raw_action - prev_raw)))
        if prev_exec is not None:
            action_delta_exec.append(float(np.linalg.norm(action_exec - prev_exec)))
        prev_raw = raw_action.copy()
        prev_exec = action_exec.copy()
        raw_rows.append(
            {
                **prefix_base,
                "episode_id": int(episode_id),
                "episode_seed": int(episode_seed),
                "step": int(info.get("step", steps)),
                "time": float(info.get("time", steps * fenv.dt)),
                "shield_triggered": int(triggered),
                "shield_reason": reason,
                "filter_delta_norm": filter_delta,
                "raw_action_unsafe": int(np.isfinite(raw_cpas[-1]) and 0.0 < raw_ttcs[-1] < float(shield.params["horizon"]) and raw_cpas[-1] < float(shield.params["cpa_safe"])),
                "filtered_action_unsafe": int(np.isfinite(filtered_cpas[-1]) and 0.0 < filtered_ttcs[-1] < float(shield.params["horizon"]) and filtered_cpas[-1] < float(shield.params["cpa_safe"])),
                "min_predicted_cpa_raw": raw_cpas[-1],
                "min_predicted_cpa_filtered": filtered_cpas[-1],
                "min_ttc_raw": raw_ttcs[-1],
                "min_ttc_filtered": filtered_ttcs[-1],
                "a_raw_norm": action_norm_raw[-1],
                "a_exec_norm": action_norm_exec[-1],
            }
        )
        obs, reward, terminated, truncated, info = env.step(action_exec)
        done = bool(terminated or truncated)
        steps += 1
        episode_return += float(reward)
        last_info = info
        min_distances.append(float(info.get("min_distance", np.nan)))
        add_optional_gpsi_diagnostics(
            env=env,
            model=model,
            cfg=cfg,
            prefix=prefix_base,
            info=info,
            gpsi_accum=gpsi_accum,
            feature_accum=feature_accum,
        )

    near_miss = int(float(last_info["episode_min_distance"]) < float(args.near_miss_distance) and not bool(last_info["is_collision"]))
    def mean_finite(values: list[float]) -> float:
        clean = [float(v) for v in values if np.isfinite(v)]
        return float(np.mean(clean)) if clean else float("nan")
    def min_finite(values: list[float]) -> float:
        clean = [float(v) for v in values if np.isfinite(v)]
        return float(np.min(clean)) if clean else float("nan")
    row = {
        **prefix_base,
        "episode_id": int(episode_id),
        "episode_seed": int(episode_seed),
        "success": int(bool(last_info["is_success"])),
        "collision": int(bool(last_info["is_collision"])),
        "timeout": int(bool(last_info.get("truncated", False))),
        "near_miss": near_miss,
        "progress": float(last_info["progress"]),
        "mean_min_distance": float(np.nanmean(min_distances)) if min_distances else np.nan,
        "episode_min_distance": float(last_info["episode_min_distance"]),
        "episode_length": int(steps),
        "episode_reward": float(episode_return),
        "raw_unsafe_action_rate": float(np.mean([row["raw_action_unsafe"] for row in raw_rows])) if raw_rows else np.nan,
        "raw_safe_margin_unsafe_action_rate": float(np.mean([row["raw_action_unsafe"] for row in raw_rows])) if raw_rows else np.nan,
        "action_norm": float(np.mean(action_norm_exec)) if action_norm_exec else np.nan,
        "action_delta": float(np.mean(action_delta_exec)) if action_delta_exec else 0.0,
        "no_response_rate": float(np.mean([float(v < 0.05) for v in action_norm_exec])) if action_norm_exec else np.nan,
        "threat_class": str(last_info.get("threat_class", "none")),
        "threat_motion_mode": str(last_info.get("threat_motion_mode", "none")),
        "planned_cpa": float(last_info.get("planned_cpa_to_threat", np.nan)),
        "planned_ttc": float(last_info.get("planned_ttc_to_threat", np.nan)),
        "episode_filter_triggered": int(filter_triggered_any),
        "shield_trigger_rate": float(filter_triggers / max(steps, 1)),
        "shield_intervention_steps": int(filter_triggers),
        "filter_delta_norm_mean": float(np.mean(filter_deltas)) if filter_deltas else 0.0,
        "filter_delta_norm_p95": float(np.percentile(filter_deltas, 95)) if filter_deltas else 0.0,
        "max_filter_delta_norm": float(np.max(filter_deltas)) if filter_deltas else 0.0,
        "raw_action_unsafe_rate": float(np.mean([row["raw_action_unsafe"] for row in raw_rows])) if raw_rows else np.nan,
        "filtered_action_unsafe_rate": float(np.mean([row["filtered_action_unsafe"] for row in raw_rows])) if raw_rows else np.nan,
        "raw_min_predicted_cpa": mean_finite(raw_cpas),
        "filtered_min_predicted_cpa": mean_finite(filtered_cpas),
        "raw_ttc_min": min_finite(raw_ttcs),
        "filtered_ttc_min": min_finite(filtered_ttcs),
        "a_raw_norm": float(np.mean(action_norm_raw)) if action_norm_raw else np.nan,
        "a_exec_norm": float(np.mean(action_norm_exec)) if action_norm_exec else np.nan,
        "action_delta_raw": float(np.mean(action_delta_raw)) if action_delta_raw else 0.0,
        "action_delta_exec": float(np.mean(action_delta_exec)) if action_delta_exec else 0.0,
        "progress_loss_due_to_filter": 0.0,
        "nan_or_crash": 0,
    }
    return row, raw_rows


