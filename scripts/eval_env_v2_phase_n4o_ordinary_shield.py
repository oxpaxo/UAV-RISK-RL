def summarize_raw(rows: list[dict[str, Any]]) -> list[dict[str, Any]]:
    if not rows:
        return []
    df = pd.DataFrame(rows)
    group_cols = ["method_key", "method", "base_policy", "shield_enabled", "shield_config_name", "eval_seed", "scenario"]
    out = (
        df.groupby(group_cols, dropna=False)
        .agg(
            steps=("step", "count"),
            shield_trigger_rate=("shield_triggered", "mean"),
            filter_delta_norm_mean=("filter_delta_norm", "mean"),
            filter_delta_norm_p95=("filter_delta_norm", lambda s: float(np.percentile(pd.to_numeric(s, errors="coerce").dropna(), 95)) if len(pd.to_numeric(s, errors="coerce").dropna()) else np.nan),
            raw_action_unsafe_rate=("raw_action_unsafe", "mean"),
            filtered_action_unsafe_rate=("filtered_action_unsafe", "mean"),
            raw_min_predicted_cpa=("min_predicted_cpa_raw", "mean"),
            filtered_min_predicted_cpa=("min_predicted_cpa_filtered", "mean"),
            raw_ttc_min=("min_ttc_raw", "min"),
            filtered_ttc_min=("min_ttc_filtered", "min"),
            a_raw_norm=("a_raw_norm", "mean"),
            a_exec_norm=("a_exec_norm", "mean"),
        )
        .reset_index()
    )
    return out.to_dict("records")


def feature_summary_rows(feature_accum: dict[tuple[Any, ...], dict[str, base_eval.BlockAccumulator]], cfg_by_method: dict[str, dict[str, Any]]) -> list[dict[str, Any]]:
    rows: list[dict[str, Any]] = []
    blocks = ["obs_i_12", "delta_hat_9_after_scale", "logvar_raw_9_clamped", "logvar_scaled_9_policy", "adapter_output_64", "full_aug_obs"]
    for key, groups in feature_accum.items():
        method_key, method, checkpoint, step, label, scenario, motion_mode, threat_class = key
        cfg = cfg_by_method.get(str(method_key), {})
        prefix = {
            "method_key": method_key,
            "method": method,
            "checkpoint": checkpoint,
            "checkpoint_step": int(step),
            "checkpoint_label": label,
            "scenario": scenario,
            "motion_mode": motion_mode,
            "threat_class": threat_class,
            "feature_adapter": str(cfg.get("ppo", {}).get("feature_adapter", "")) if cfg else "attention_full",
        }
        for block in blocks:
            if block in groups:
                rows.append(groups[block].row(prefix, block))
            else:
                rows.append(base_eval.BlockAccumulator().row(prefix, block, not_applicable=True))
    return rows


def phase_b_context_rows(args: argparse.Namespace) -> list[dict[str, Any]]:
    path = ROOT / args.phase_b_dir / "tables/phase_b_eval_summary.csv"
    if not path.exists():
        raise N4OEvalStop("phase_b_config_missing", f"missing Phase B eval summary: {rel(path)}")
    df = pd.read_csv(path)
    use = df[(df["stage"].astype(str) == "b2_formal") & (df["config_name"].astype(str).isin(["vo_like_filter_h45_cpa1p2_h16", "cpa_ttc_weighted_apf_alpha3"]))].copy()
    rows = []
    for _, row in use.iterrows():
        rows.append(
            {
                "context_config": row["config_name"],
                "baseline_name": row["baseline_name"],
                "success_rate": float(row["success_rate"]),
                "collision_rate": float(row["collision_rate"]),
                "near_miss_rate": float(row["near_miss_rate"]),
                "progress": float(row["progress_mean"]),
                "filter_trigger_rate": float(row.get("filter_trigger_rate", np.nan)),
                "source": "Phase B formal B2",
            }
        )
    return rows


def main() -> None:
    args = parse_args()
    result_dir = ROOT / args.result_dir
    ensure_dirs(result_dir)
    try:
        shield = load_phase_b_shield(args)
        if any(key in shield.params for key in ["sigma2", "uncertainty", "directional_margin"]):
            raise N4OEvalStop("sigma_used", phase_b.json_params(shield.params))
        specs = policy_specs()
        write_checkpoint_manifest(result_dir, specs)
        write_shield_manifest(result_dir, shield)
        write_csv(result_dir / "tables/phase_n4o_phase_b_context_comparison.csv", phase_b_context_rows(args))
        append_csv(
            result_dir / "tables/phase_n4o_command_manifest.csv",
            [
                {
                    "stage": "eval",
                    "command": " ".join(["python", *sys.argv]),
                    "eval_seeds": json.dumps([int(seed) for seed in args.eval_seeds]),
                    "num_episodes": int(args.num_episodes),
                    "scenarios": json.dumps(args.scenarios),
                    "shield_config_name": shield.config_name,
                    "device": args.device,
                }
            ],
        )
        runtime_args = eval_args(args)
        all_episode_rows: list[dict[str, Any]] = []
        all_raw_rows: list[dict[str, Any]] = []
        gpsi_accum: dict[tuple[Any, ...], dict[str, base_eval.ScalarAccumulator]] = {}
        feature_accum: dict[tuple[Any, ...], dict[str, base_eval.BlockAccumulator]] = {}
        cfg_by_method: dict[str, dict[str, Any]] = {}
        torch.manual_seed(0)
        np.random.seed(0)

        for method_key, spec in specs.items():
            cfg = load_yaml(ROOT / str(spec["cfg"])) if spec["cfg"] else None
            if cfg is not None and spec["base_policy"] == "p3_1500k":
                cfg["method_key"] = "block_projected"
            if cfg is not None:
                cfg_by_method[method_key] = cfg
            model = load_policy_model(spec, args)
            for eval_seed in args.eval_seeds:
                for scenario in args.scenarios:
                    env = make_env(spec, scenario, args, runtime_args)
                    start = time.time()
                    last = start
                    print(
                        f"[{time.strftime('%Y-%m-%d %H:%M:%S')}] N4O_EVAL_START "
                        f"method={method_key} eval_seed={eval_seed} scenario={scenario} episodes={args.num_episodes}",
                        flush=True,
                    )
                    for episode_id in range(int(args.num_episodes)):
                        episode_seed = int(eval_seed) + episode_id
                        ep_row, raw_rows = evaluate_episode(
                            model=model,
                            env=env,
                            cfg=cfg,
                            method_key=method_key,
                            spec=spec,
                            shield=shield,
                            scenario=scenario,
                            eval_seed=int(eval_seed),
                            episode_id=(int(eval_seed) - min(args.eval_seeds)) * int(args.num_episodes) + episode_id,
                            episode_seed=episode_seed,
                            args=args,
                            runtime_args=runtime_args,
                            gpsi_accum=gpsi_accum,
                            feature_accum=feature_accum,
                        )
                        all_episode_rows.append(ep_row)
                        all_raw_rows.extend(raw_rows)
                        now = time.time()
                        if now - last >= float(args.heartbeat_seconds) or episode_id == int(args.num_episodes) - 1:
                            done = episode_id + 1
                            rate = done / max(now - start, 1e-6)
                            print(
                                f"[{time.strftime('%Y-%m-%d %H:%M:%S')}] N4O_EVAL_HEARTBEAT "
                                f"method={method_key} eval_seed={eval_seed} scenario={scenario} "
                                f"episodes={done}/{args.num_episodes} rate={rate:.2f} ep/s",
                                flush=True,
                            )
                            last = now
                    env.close()

        by_seed_cols = ["method_key", "method", "base_policy", "shield_enabled", "shield_config_name", "checkpoint_path", "checkpoint_label", "eval_seed"]
        write_csv(result_dir / "tables/phase_n4o_episode_metrics.csv", all_episode_rows)
        write_csv(result_dir / "tables/phase_n4o_eval_summary_by_seed.csv", summarize_episode_rows(all_episode_rows, by_seed_cols))
        write_csv(result_dir / "tables/phase_n4o_scenario_breakdown.csv", summarize_episode_rows(all_episode_rows, by_seed_cols + ["scenario"]))
        write_csv(result_dir / "tables/phase_n4o_motion_mode_breakdown.csv", summarize_episode_rows(all_episode_rows, by_seed_cols + ["threat_motion_mode"]))
        write_csv(result_dir / "tables/phase_n4o_threat_class_breakdown.csv", summarize_episode_rows(all_episode_rows, by_seed_cols + ["threat_class"]))
        write_csv(result_dir / "tables/phase_n4o_raw_vs_filtered_safety.csv", summarize_raw(all_raw_rows))
        write_csv(result_dir / "tables/phase_n4o_raw_vs_filtered_safety_steps.csv", all_raw_rows)
        write_csv(result_dir / "tables/phase_n4o_gpsi_output_summary.csv", base_eval.gpsi_summary_rows(gpsi_accum))
        write_csv(result_dir / "tables/phase_n4o_feature_block_stats.csv", feature_summary_rows(feature_accum, cfg_by_method))
        print(
            f"[{time.strftime('%Y-%m-%d %H:%M:%S')}] N4O_EVAL_END episodes={len(all_episode_rows)} raw_steps={len(all_raw_rows)}",
            flush=True,
        )
    except N4OEvalStop as exc:
        write_stop(result_dir, exc.reason, exc.detail)
        raise SystemExit(2) from exc
    except Exception:
        write_stop(result_dir, "eval_failed", traceback.format_exc())
        raise SystemExit(2)


if __name__ == "__main__":
    main()
